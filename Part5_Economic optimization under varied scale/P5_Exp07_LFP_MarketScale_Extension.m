function grid_values = local_make_grid(lower_value, upper_value, step_value)
% Construct an inclusive increasing grid with a specified step.

    lower_value = double(lower_value);
    upper_value = double(upper_value);
    step_value = double(step_value);

    if ~isscalar(lower_value) || ~isscalar(upper_value) || ...
            ~isscalar(step_value) || ...
            ~isfinite(lower_value) || ~isfinite(upper_value) || ...
            ~isfinite(step_value) || step_value <= 0 || ...
            lower_value >= upper_value
        error('Invalid threshold-grid specification.');
    end

    grid_values = (lower_value:step_value:upper_value).';

    if abs(grid_values(end) - upper_value) > 1e-12
        grid_values(end + 1, 1) = upper_value;
    end

    grid_values = unique(grid_values, 'stable').';
end

function P = local_evaluate_fitted_residual_parameters( ...
        k_query, E, k_anchor, muR_constant)
% Evaluate the fitted Part3 residual equations at arbitrary k >= anchor.

    k_query = double(k_query(:));
    k_emp = double(E.k_emp(:));

    if any(~isfinite(k_query)) || any(k_query < k_anchor)
        error('Extended residual parameters require finite k >= k_anchor.');
    end

    idx_k0 = find(k_emp == min(k_emp), 1);
    PR0 = double(E.P_R_fit_input(idx_k0));
    k0 = min(k_emp);

    idx_anchor = find(k_emp == k_anchor, 1);

    if isempty(idx_anchor)
        error('k_anchor is not present in the Part3 empirical k set.');
    end

    muM_anchor = double(E.mu_M_fit_input(idx_anchor));

    sigR_input = double(E.sig_R_fit_input(:));

    mask_sigR = ...
        (k_emp >= E.cfg.k_emp_min) & ...
        (k_emp <= k_anchor) & ...
        (sigR_input > 0) & ...
        isfinite(sigR_input);

    if ~any(mask_sigR)
        error('Unable to reconstruct the sigma_R fitting origin.');
    end

    k0_sigR = min(k_emp(mask_sigR));

    P = struct();

    P.rmse_target = ...
        double(E.a_RMSE) .* k_query .^ (-double(E.b_RMSE)) + ...
        double(E.c_RMSE);

    P.PR_curve = min(max( ...
        double(E.cR_opt) + ...
        (PR0 - double(E.cR_opt)) .* ...
        exp(-double(E.bR_opt) .* (k_query - k0)), ...
        0), 0.40);

    P.muM_curve = ...
        muM_anchor .* ...
        exp(-double(E.a_muM) .* (k_query - k_anchor));

    P.muR_curve = repmat(double(muR_constant), numel(k_query), 1);

    P.sigR_curve = max( ...
        double(E.c_sigR) + ...
        double(E.A_sigR) .* ...
        exp(-double(E.b_sigR) .* (k_query - k0_sigR)), ...
        1e-6);

    P.w_curve = zeros(numel(k_query), 1);
    P.sigM_curve = zeros(numel(k_query), 1);
    P.PR_model = zeros(numel(k_query), 1);
    P.w_feasible_max = zeros(numel(k_query), 1);
    P.w_solver_status = strings(numel(k_query), 1);

    for ii = 1:numel(k_query)
        [P.w_curve(ii), ...
            P.sigM_curve(ii), ...
            P.PR_model(ii), ...
            P.w_feasible_max(ii), ...
            status_now] = local_solve_region_consistent_weight( ...
            P.PR_curve(ii), ...
            P.rmse_target(ii), ...
            P.muM_curve(ii), ...
            P.muR_curve(ii), ...
            P.sigR_curve(ii), ...
            E.region.right, ...
            E.cfg.min_sig_main);

        P.w_solver_status(ii) = string(status_now);
    end
end

function [O_by_action, A_by_action, R_by_action, Total_by_action] = ...
        local_build_candidate_action_costs( ...
        Pi_True, Pi_True_Max, True_Action_i, ...
        Eta_1, Eta_2, Lambda_A, Lambda_R)
% Precompute per-sample costs for assigning each candidate action.

    N_sim = numel(True_Action_i);

    O_by_action = zeros(N_sim, 3);
    A_by_action = zeros(N_sim, 3);
    R_by_action = zeros(N_sim, 3);

    for action_now = 1:3
        O_now = max(Pi_True_Max - Pi_True(:, action_now), 0);

        is_G1_error = ...
            (True_Action_i == 3 & action_now == 2) | ...
            (True_Action_i == 2 & action_now == 3);

        is_G2_error = ...
            (True_Action_i == 1 & action_now == 2) | ...
            (True_Action_i == 2 & action_now == 1);

        eta_now = zeros(N_sim, 1);
        eta_now(is_G1_error) = Eta_1;
        eta_now(is_G2_error) = Eta_2;

        trigger_now = O_now > eta_now;
        excess_now = max(0, O_now - eta_now);

        A_now = trigger_now .* Lambda_A;

        R_now = (action_now < True_Action_i) .* ...
            trigger_now .* (Lambda_R .* excess_now);

        O_by_action(:, action_now) = O_now;
        A_by_action(:, action_now) = A_now;
        R_by_action(:, action_now) = R_now;
    end

    Total_by_action = O_by_action + A_by_action + R_by_action;
end

function Q = local_optimize_thresholds_fast( ...
        s_pred, Pi_True, ...
        O_by_action, A_by_action, R_by_action, Total_by_action, ...
        T1_grid, T2_grid)
% Find the exact grid optimum using cumulative sums after sorting s_pred.
%
% For one threshold pair:
%   s_pred < T1        -> A3
%   T1 <= s_pred < T2  -> A2
%   s_pred >= T2       -> A1
%
% The cost of assigning each sample to A1/A2/A3 is threshold-independent.
% Therefore, cumulative sums reproduce the original brute-force objective
% exactly while avoiding sample-level recalculation for every grid pair.

    s_pred = double(s_pred(:));
    N_sim = numel(s_pred);

    [s_sorted, sort_idx] = sort(s_pred, 'ascend');
    sorted_cost = Total_by_action(sort_idx, :);

    cumulative_cost = [zeros(1, 3); cumsum(sorted_cost, 1)];

    n_below_T1 = zeros(numel(T1_grid), 1);
    n_below_T2 = zeros(numel(T2_grid), 1);

    for ii = 1:numel(T1_grid)
        n_below_T1(ii) = local_count_less_than(s_sorted, T1_grid(ii));
    end

    for jj = 1:numel(T2_grid)
        n_below_T2(jj) = local_count_less_than(s_sorted, T2_grid(jj));
    end

    best_loss = inf;
    best_T1 = NaN;
    best_T2 = NaN;

    for ii = 1:numel(T1_grid)
        T1_now = T1_grid(ii);
        n1 = n_below_T1(ii);

        for jj = 1:numel(T2_grid)
            T2_now = T2_grid(jj);

            if T1_now >= T2_now
                continue;
            end

            n2 = n_below_T2(jj);

            cost_A3 = cumulative_cost(n1 + 1, 3);
            cost_A2 = ...
                cumulative_cost(n2 + 1, 2) - ...
                cumulative_cost(n1 + 1, 2);
            cost_A1 = ...
                cumulative_cost(end, 1) - ...
                cumulative_cost(n2 + 1, 1);

            loss_now = (cost_A3 + cost_A2 + cost_A1) / N_sim;

            % Strict inequality preserves the original first-minimum
            % tie-breaking order of the nested T1/T2 loops.
            if loss_now < best_loss
                best_loss = loss_now;
                best_T1 = T1_now;
                best_T2 = T2_now;
            end
        end
    end

    if ~isfinite(best_loss)
        error('No valid T1/T2 pair was found.');
    end

    Pred_Action = 3 * ones(N_sim, 1);
    Pred_Action(s_pred >= best_T1 & s_pred < best_T2) = 2;
    Pred_Action(s_pred >= best_T2) = 1;

    idx_pred = sub2ind([N_sim, 3], (1:N_sim)', Pred_Action);

    Q = struct();
    Q.T1_star = best_T1;
    Q.T2_star = best_T2;

    Q.O_per_pack = mean(O_by_action(idx_pred));
    Q.A_per_pack = mean(A_by_action(idx_pred));
    Q.R_per_pack = mean(R_by_action(idx_pred));

    Q.action_share = [ ...
        mean(Pred_Action == 1), ...
        mean(Pred_Action == 2), ...
        mean(Pred_Action == 3)];

    Q.A1_value_perpack = ...
        mean(Pi_True(:, 1) .* (Pred_Action == 1));

    Q.A2_value_perpack = ...
        mean(Pi_True(:, 2) .* (Pred_Action == 2));

    Q.A3_value_perpack = ...
        mean(Pi_True(:, 3) .* (Pred_Action == 3));

    Q.decision_loss_per_pack = best_loss;

    accounting_error = abs( ...
        best_loss - ...
        (Q.O_per_pack + Q.A_per_pack + Q.R_per_pack));

    if accounting_error > 1e-10
        error(['Fast threshold optimization accounting mismatch: ' ...
            '%.6g USD per pack.'], accounting_error);
    end
end

function count_value = local_count_less_than(sorted_values, threshold)
% Count sorted values strictly below a threshold using binary search.

    n = numel(sorted_values);

    if n == 0 || sorted_values(1) >= threshold
        count_value = 0;
        return;
    end

    if sorted_values(end) < threshold
        count_value = n;
        return;
    end

    lower_idx = 1;
    upper_idx = n;

    while lower_idx < upper_idx
        middle_idx = floor((lower_idx + upper_idx + 1) / 2);

        if sorted_values(middle_idx) < threshold
            lower_idx = middle_idx;
        else
            upper_idx = middle_idx - 1;
        end
    end

    count_value = lower_idx;
end

function [w_opt, sigM_opt, PR_model, w_feasible_max, status] = ...
        local_solve_region_consistent_weight( ...
        PR_target, rmse_target, muM, muR, sigR, ...
        right_region, min_sigM)
% Solve the latent right-component weight while matching target RMSE and
% probability inside the fixed right-tail diagnostic interval.

    PR_target = min(max(PR_target, 0), 1);
    rmse_target = max(rmse_target, 1e-12);
    min_sigM = max(min_sigM, 1e-8);

    second_right = sigR^2 + muR^2;
    second_main_min = min_sigM^2 + muM^2;

    if rmse_target^2 <= second_main_min
        w_feasible_max = 0;
        w_opt = 0;
        sigM_opt = sqrt(max( ...
            rmse_target^2 - muM^2, min_sigM^2));
        PR_model = local_region_probability_mixture( ...
            w_opt, muM, sigM_opt, muR, sigR, right_region);
        status = 'No positive-w solution: main-mode floor active';
        return;
    end

    if second_right > second_main_min
        w_feasible_max = ...
            (rmse_target^2 - second_main_min) / ...
            (second_right - second_main_min);
    else
        w_feasible_max = 0.999;
    end

    w_feasible_max = min(max(w_feasible_max, 0), 0.999);

    if w_feasible_max <= 1e-12
        w_opt = 0;
        sigM_opt = sqrt(max( ...
            rmse_target^2 - muM^2, min_sigM^2));
        PR_model = local_region_probability_mixture( ...
            w_opt, muM, sigM_opt, muR, sigR, right_region);
        status = 'Boundary solution: w = 0';
        return;
    end

    objective = @(w) local_region_probability_error( ...
        w, PR_target, rmse_target, ...
        muM, muR, sigR, right_region, min_sigM);

    [w_opt, ~] = fminbnd( ...
        objective, 0, w_feasible_max, ...
        optimset('Display', 'off', 'TolX', 1e-10));

    sigM_opt = local_sigma_main_from_rmse( ...
        w_opt, rmse_target, muM, muR, sigR, min_sigM);

    PR_model = local_region_probability_mixture( ...
        w_opt, muM, sigM_opt, muR, sigR, right_region);

    if w_opt < 1e-7
        status = 'Boundary solution near w = 0';
    elseif abs(w_opt - w_feasible_max) < 1e-7
        status = 'Boundary solution near feasible maximum';
    else
        status = 'Interior solution';
    end
end

function value = local_region_probability_error( ...
        w, PR_target, rmse_target, ...
        muM, muR, sigR, right_region, min_sigM)
% Squared normalized discrepancy between model and target right-region mass.

    sigM = local_sigma_main_from_rmse( ...
        w, rmse_target, muM, muR, sigR, min_sigM);

    PR_model = local_region_probability_mixture( ...
        w, muM, sigM, muR, sigR, right_region);

    scale = max(PR_target, 0.01);
    value = ((PR_model - PR_target) / scale)^2;
end

function sigM = local_sigma_main_from_rmse( ...
        w, rmse_target, muM, muR, sigR, min_sigM)
% Back-calculate the main-component width from the target second moment.

    numerator = ...
        rmse_target^2 ...
        - w * (sigR^2 + muR^2) ...
        - (1 - w) * muM^2;

    denominator = max(1 - w, 1e-12);

    sigM = sqrt(max( ...
        numerator / denominator, ...
        min_sigM^2));
end

function PR_model = local_region_probability_mixture( ...
        w, muM, sigM, muR, sigR, right_region)
% Probability of the two-normal mixture inside the fixed right interval.

    lower_bound = right_region(1);
    upper_bound = right_region(2);

    q_main = ...
        normcdf(upper_bound, muM, sigM) - ...
        normcdf(lower_bound, muM, sigM);

    q_right = ...
        normcdf(upper_bound, muR, sigR) - ...
        normcdf(lower_bound, muR, sigR);

    PR_model = (1 - w) * q_main + w * q_right;
end
