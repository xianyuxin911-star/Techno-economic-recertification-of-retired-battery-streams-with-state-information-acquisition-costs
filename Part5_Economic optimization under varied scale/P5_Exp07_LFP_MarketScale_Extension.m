% extended market-scale throughput scenarios.
% It extends the deployment scale from the manuscript range of 10-2,000 tons
% to exploratory market-scale scenarios up to 500,000 tons. The script reports
% the scale-specific optimal k*, thresholds, cost decomposition, pathway values,
% and net value after recertification cost.

