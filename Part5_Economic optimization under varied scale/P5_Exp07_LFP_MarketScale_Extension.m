%% P5_Exp07_LFP_MarketScale_Extension.m
% Purpose:
% This script extends the Part5 Exp01 LFP market-scale analysis to
% exploratory throughputs from 5,000 to 500,000 tons.
%
% The established k = 5-50 results from Part5 Exp01 are retained exactly.
% The script extends only k = 51-2000 by evaluating the fitted Part3
% residual-error equations beyond k = 50, using the same Monte Carlo
% population and common random numbers saved by Part5 Exp01.
%
% For each new k, T1 and T2 are re-optimized on a 0.005 threshold grid.
% The existing and newly evaluated k-dependent results are then combined,
% and the market-scale optimum is selected over k = 5-2000.

clear;
clc;
close all;

format compact;
format shortG;

%% 0) Path configuration
cfg = struct();

script_dir = fileparts(mfilename('fullpath'));

if isempty(script_dir)
    script_dir = pwd;
end

cfg.project_root = fileparts(script_dir);

cfg.output_dir = fullfile(script_dir, 'Output');
cfg.results_dir = fullfile(cfg.output_dir, 'Results');
cfg.function_dir = fullfile(script_dir, 'Function');

cfg.lfp_result_primary = fullfile(cfg.results_dir, ...
    'P5_Exp01_ScaleDependent_LFP_Results.mat');

cfg.lfp_result_legacy = fullfile(cfg.output_dir, ...
    'Result01_ScaleEffect_LFP_Results.mat');

cfg.lfp_workspace = fullfile(cfg.results_dir, ...
    'P5_Exp01_ScaleDependent_LFP_Workspace.mat');

cfg.residual_model = fullfile(cfg.project_root, ...
    'Part3_Prediction error extrapolation', ...
    'Output', 'Results', ...
    'P3_Exp03_ResidualExtrapolation_Model.mat');

cfg.market_scale_csv = fullfile(cfg.results_dir, ...
    'P5_Exp07_LFP_MarketScale_Extension_Table.csv');

cfg.cost_breakdown_csv = fullfile(cfg.results_dir, ...
    'P5_Exp07_LFP_MarketScale_CostBreakdown.csv');

cfg.market_only_csv = fullfile(cfg.results_dir, ...
    'P5_Exp07_LFP_MarketScale_OnlyRows.csv');

cfg.extra_k_csv = fullfile(cfg.results_dir, ...
    'P5_Exp07_LFP_K51_K2000_Extension.csv');

cfg.result_mat = fullfile(cfg.results_dir, ...
    'P5_Exp07_LFP_MarketScale_Extension_Results.mat');

% Established and extended k ranges.
cfg.base_k_max = 50;
cfg.extended_k_max = 2000;

% The new k = 51-2000 threshold searches use this resolution.
cfg.threshold_step = 0.005;

% Exploratory market-throughput scenarios.
cfg.scale_ton_list = [ ...
    5000; 10000; 20000; 50000; ...
    100000; 200000; 300000; 500000];

% LFP pack mass used in Part5 Exp01.
cfg.M_pack_kg = 173.25;

cfg.accounting_tolerance_abs_USD = 1e-5;
cfg.accounting_tolerance_rel = 1e-10;
cfg.parameter_continuity_tolerance = 1e-8;

if ~exist(cfg.results_dir, 'dir')
    mkdir(cfg.results_dir);
end

if exist(cfg.function_dir, 'dir')
    addpath(cfg.function_dir);
end

if exist('local_build_noise_by_k', 'file') ~= 2
    error('Function local_build_noise_by_k.m was not found in: %s', ...
        cfg.function_dir);
end

if isfile(cfg.lfp_result_primary)
    cfg.input_file = cfg.lfp_result_primary;
elseif isfile(cfg.lfp_result_legacy)
    cfg.input_file = cfg.lfp_result_legacy;
else
    error(['Part5 Exp01 LFP result was not found.\nChecked:\n%s\n%s\n' ...
        'Run Part5 Exp01 before running Exp07.'], ...
        cfg.lfp_result_primary, cfg.lfp_result_legacy);
end

if ~isfile(cfg.lfp_workspace)
    error(['Part5 Exp01 workspace was not found:\n%s\n' ...
        'Run Part5 Exp01 before running the k extension.'], ...
        cfg.lfp_workspace);
end

if ~isfile(cfg.residual_model)
    error(['Part3 residual-error model was not found:\n%s\n' ...
        'Run Part3 Exp03 before running the k extension.'], ...
        cfg.residual_model);
end

fprintf('================ Part5 Exp07 LFP market-scale k extension ================\n');
fprintf('Input Part5 Exp01 result: %s\n', cfg.input_file);
fprintf('Input Part5 Exp01 workspace: %s\n', cfg.lfp_workspace);
fprintf('Input Part3 residual model: %s\n', cfg.residual_model);
fprintf('New k range: %d to %d\n', cfg.base_k_max + 1, cfg.extended_k_max);
fprintf('New threshold-grid step: %.4f\n', cfg.threshold_step);
fprintf('Market-scale table: %s\n', cfg.market_scale_csv);
fprintf('Result MAT: %s\n', cfg.result_mat);

%% 1) Load and validate the established k = 5-50 results
S = load(cfg.input_file);
R = S;

field_names = fieldnames(S);

for ii = 1:numel(field_names)
    field_now = field_names{ii};

    if isstruct(S.(field_now)) && ...
            isfield(S.(field_now), 'k_vals') && ...
            isfield(S.(field_now), 'O_base')
        R = S.(field_now);
        fprintf('Using result structure: %s\n', field_now);
        break;
    end
end

required_fields = { ...
    'k_vals', ...
    'O_base', ...
    'A_base', ...
    'R_base', ...
    'T1_base', ...
    'T2_base', ...
    'Pred_Action_Ratio_byK', ...
    'A1_value_perpack_byK', ...
    'A2_value_perpack_byK', ...
    'A3_value_perpack_byK', ...
    'Model_Cost_NK', ...
    'rmse_target', ...
    'w_curve', ...
    'muM_curve', ...
    'sigM_curve', ...
    'muR_curve', ...
    'sigR_curve', ...
    'k_emp', ...
    'k_anchor', ...
    'k_bridge_end'};

for ii = 1:numel(required_fields)
    field_now = required_fields{ii};

    if ~isfield(R, field_now)
        error('Required Part5 Exp01 field is missing: %s', field_now);
    end
end

k_vals_base = double(R.k_vals(:));
O_base_existing = double(R.O_base(:));
A_base_existing = double(R.A_base(:));
R_base_existing = double(R.R_base(:));

T1_base_existing = double(R.T1_base(:));
T2_base_existing = double(R.T2_base(:));

Pred_Action_Ratio_existing = double(R.Pred_Action_Ratio_byK);
A1_value_existing = double(R.A1_value_perpack_byK(:));
A2_value_existing = double(R.A2_value_perpack_byK(:));
A3_value_existing = double(R.A3_value_perpack_byK(:));

rmse_existing = double(R.rmse_target(:));
w_existing = double(R.w_curve(:));
muM_existing = double(R.muM_curve(:));
sigM_existing = double(R.sigM_curve(:));
muR_existing = double(R.muR_curve(:));
sigR_existing = double(R.sigR_curve(:));

k_emp = double(R.k_emp(:));
k_anchor = double(R.k_anchor);
k_bridge_end = double(R.k_bridge_end);

Model_Cost_NK_upstream = double(R.Model_Cost_NK);

num_k_base = numel(k_vals_base);

expected_lengths = [ ...
    numel(O_base_existing), ...
    numel(A_base_existing), ...
    numel(R_base_existing), ...
    numel(T1_base_existing), ...
    numel(T2_base_existing), ...
    numel(A1_value_existing), ...
    numel(A2_value_existing), ...
    numel(A3_value_existing), ...
    numel(rmse_existing), ...
    numel(w_existing), ...
    numel(muM_existing), ...
    numel(sigM_existing), ...
    numel(muR_existing), ...
    numel(sigR_existing)];

if any(expected_lengths ~= num_k_base)
    error('The k-dependent Part5 Exp01 arrays have inconsistent lengths.');
end

if ~isequal(size(Pred_Action_Ratio_existing), [num_k_base, 3])
    error('Pred_Action_Ratio_byK must have size [num_k, 3].');
end

if any(~isfinite(k_vals_base)) || any(diff(k_vals_base) ~= 1)
    error('The established k values must be consecutive increasing integers.');
end

if max(k_vals_base) ~= cfg.base_k_max
    error(['The established Part5 Exp01 result must end at k = %d, ' ...
        'but the loaded result ends at k = %d.'], ...
        cfg.base_k_max, max(k_vals_base));
end

if any([O_base_existing; A_base_existing; R_base_existing] < -1e-10)
    error('Per-pack decision-cost components must be non-negative.');
end

% Recover the information-cost equation from the upstream cost matrix.
if size(Model_Cost_NK_upstream, 2) == num_k_base
    model_cost_by_k = Model_Cost_NK_upstream(1, :).';
elseif size(Model_Cost_NK_upstream, 1) == num_k_base
    model_cost_by_k = Model_Cost_NK_upstream(:, 1);
else
    error('Model_Cost_NK has an unexpected shape.');
end

if num_k_base < 2
    error('At least two k values are required to recover information cost.');
end

c_pack = median(diff(model_cost_by_k) ./ diff(k_vals_base));
c_train_by_k = model_cost_by_k - k_vals_base .* c_pack;
c_train = median(c_train_by_k);

model_cost_reconstruction_error = max(abs( ...
    model_cost_by_k - (k_vals_base .* c_pack + c_train)));

if model_cost_reconstruction_error > 1e-6
    error(['The upstream information-cost curve is not linear in k. ' ...
        'Inspect Model_Cost_NK before extending the k range.']);
end

fprintf('>>> Established k range loaded: %d to %d\n', ...
    min(k_vals_base), max(k_vals_base));
fprintf('>>> Recovered c_pack = %.4f USD and c_train = %.4f USD\n', ...
    c_pack, c_train);

%% 2) Load the saved Monte Carlo population and Part3 fit parameters
workspace_vars = { ...
    's_true', ...
    'Pi_True', ...
    'Pi_True_Max', ...
    'True_Action_i', ...
    'Eta_1', ...
    'Eta_2', ...
    'Lambda_A', ...
    'Lambda_R', ...
    'base_u', ...
    'base_z', ...
    'base_pick', ...
    'base_bridge', ...
    'T1_grid', ...
    'T2_grid'};

W = load(cfg.lfp_workspace, workspace_vars{:});

for ii = 1:numel(workspace_vars)
    if ~isfield(W, workspace_vars{ii})
        error('Required workspace variable is missing: %s', ...
            workspace_vars{ii});
    end
end

s_true = double(W.s_true(:));
Pi_True = double(W.Pi_True);
Pi_True_Max = double(W.Pi_True_Max(:));
True_Action_i = double(W.True_Action_i(:));

Eta_1 = double(W.Eta_1);
Eta_2 = double(W.Eta_2);
Lambda_A = double(W.Lambda_A);
Lambda_R = double(W.Lambda_R);

base_u = double(W.base_u(:));
base_z = double(W.base_z(:));
base_pick = double(W.base_pick(:));
base_bridge = double(W.base_bridge(:));

N_sim = numel(s_true);

if ~isequal(size(Pi_True), [N_sim, 3])
    error('Pi_True must have size [N_sim, 3].');
end

if numel(Pi_True_Max) ~= N_sim || numel(True_Action_i) ~= N_sim
    error('Pi_True_Max and True_Action_i must have length N_sim.');
end

if any([numel(base_u), numel(base_z), numel(base_pick), ...
        numel(base_bridge)] ~= N_sim)
    error('The saved common-random-number arrays must have length N_sim.');
end

T1_grid_extra = local_make_grid( ...
    min(double(W.T1_grid(:))), ...
    max(double(W.T1_grid(:))), ...
    cfg.threshold_step);

T2_grid_extra = local_make_grid( ...
    min(double(W.T2_grid(:))), ...
    max(double(W.T2_grid(:))), ...
    cfg.threshold_step);

E = load(cfg.residual_model);

required_model_fields = { ...
    'cfg', ...
    'region', ...
    'k_emp', ...
    'err_cell', ...
    'a_RMSE', ...
    'b_RMSE', ...
    'c_RMSE', ...
    'bR_opt', ...
    'cR_opt', ...
    'a_muM', ...
    'A_sigR', ...
    'b_sigR', ...
    'c_sigR', ...
    'P_R_fit_input', ...
    'mu_M_fit_input', ...
    'sig_R_fit_input'};

for ii = 1:numel(required_model_fields)
    if ~isfield(E, required_model_fields{ii})
        error('Required Part3 residual-model field is missing: %s', ...
            required_model_fields{ii});
    end
end

if ~isfield(E.cfg, 'min_sig_main')
    error('Part3 model cfg.min_sig_main is missing.');
end

if isfield(E.cfg, 'w_method') && ...
        ~strcmpi(E.cfg.w_method, 'region_consistent')
    error(['This extension expects the official region-consistent ' ...
        'right-component weight method. Loaded method: %s'], ...
        E.cfg.w_method);
end

if ~isequal(double(E.k_emp(:)), k_emp)
    error('The Part3 and Part5 empirical k sets are inconsistent.');
end

k_vals_extra = ((cfg.base_k_max + 1):cfg.extended_k_max).';

% Re-evaluate the saved fitted equations at k = 50 to verify that the
% extension reproduces the final point of the established model exactly.
P_check = local_evaluate_fitted_residual_parameters( ...
    cfg.base_k_max, E, k_anchor, median(muR_existing));

idx_base_end = find(k_vals_base == cfg.base_k_max, 1);

continuity_error = max(abs([ ...
    P_check.rmse_target - rmse_existing(idx_base_end), ...
    P_check.w_curve - w_existing(idx_base_end), ...
    P_check.muM_curve - muM_existing(idx_base_end), ...
    P_check.sigM_curve - sigM_existing(idx_base_end), ...
    P_check.muR_curve - muR_existing(idx_base_end), ...
    P_check.sigR_curve - sigR_existing(idx_base_end)]));

if continuity_error > cfg.parameter_continuity_tolerance
    error(['The fitted residual equations do not reproduce the saved ' ...
        'k = %d parameters. Maximum discrepancy = %.6g.'], ...
        cfg.base_k_max, continuity_error);
end

P_extra = local_evaluate_fitted_residual_parameters( ...
    k_vals_extra, E, k_anchor, median(muR_existing));

k_vals = [k_vals_base; k_vals_extra];

rmse_target = [rmse_existing; P_extra.rmse_target];
w_curve = [w_existing; P_extra.w_curve];
muM_curve = [muM_existing; P_extra.muM_curve];
sigM_curve = [sigM_existing; P_extra.sigM_curve];
muR_curve = [muR_existing; P_extra.muR_curve];
sigR_curve = [sigR_existing; P_extra.sigR_curve];

fprintf('>>> Residual parameter continuity at k = %d: %.3e\n', ...
    cfg.base_k_max, continuity_error);
fprintf('>>> Extended residual parameters generated through k = %d\n', ...
    max(k_vals));
fprintf('>>> Extra T1 grid: %.3f to %.3f, %d points\n', ...
    min(T1_grid_extra), max(T1_grid_extra), numel(T1_grid_extra));
fprintf('>>> Extra T2 grid: %.3f to %.3f, %d points\n', ...
    min(T2_grid_extra), max(T2_grid_extra), numel(T2_grid_extra));

%% 3) Evaluate and optimize only the new k = 51-2000 cases
[O_by_action, A_by_action, R_by_action, Total_by_action] = ...
    local_build_candidate_action_costs( ...
    Pi_True, Pi_True_Max, True_Action_i, ...
    Eta_1, Eta_2, Lambda_A, Lambda_R);

num_k_extra = numel(k_vals_extra);

O_extra = zeros(num_k_extra, 1);
A_extra = zeros(num_k_extra, 1);
R_extra = zeros(num_k_extra, 1);

T1_extra = zeros(num_k_extra, 1);
T2_extra = zeros(num_k_extra, 1);

Pred_Action_Ratio_extra = zeros(num_k_extra, 3);

A1_value_extra = zeros(num_k_extra, 1);
A2_value_extra = zeros(num_k_extra, 1);
A3_value_extra = zeros(num_k_extra, 1);

Generated_RMSE_extra = zeros(num_k_extra, 1);
Generated_bias_extra = zeros(num_k_extra, 1);
Generated_std_extra = zeros(num_k_extra, 1);

fprintf('\n>>> Evaluating new k values from %d to %d...\n', ...
    min(k_vals_extra), max(k_vals_extra));

extension_timer = tic;

for ii = 1:num_k_extra
    k_current = k_vals_extra(ii);

    [sampled_noise, noise_diagnostics] = local_build_noise_by_k( ...
        k_current, ...
        k_emp, k_anchor, k_bridge_end, ...
        E.err_cell, ...
        w_curve, muM_curve, sigM_curve, muR_curve, sigR_curve, ...
        rmse_target, k_vals, ...
        base_u, base_z, base_pick, base_bridge);

    s_pred = min(max(s_true + sampled_noise, 0), 1.0);

    Q = local_optimize_thresholds_fast( ...
        s_pred, ...
        Pi_True, ...
        O_by_action, ...
        A_by_action, ...
        R_by_action, ...
        Total_by_action, ...
        T1_grid_extra, ...
        T2_grid_extra);

    O_extra(ii) = Q.O_per_pack;
    A_extra(ii) = Q.A_per_pack;
    R_extra(ii) = Q.R_per_pack;

    T1_extra(ii) = Q.T1_star;
    T2_extra(ii) = Q.T2_star;

    Pred_Action_Ratio_extra(ii, :) = Q.action_share;

    A1_value_extra(ii) = Q.A1_value_perpack;
    A2_value_extra(ii) = Q.A2_value_perpack;
    A3_value_extra(ii) = Q.A3_value_perpack;

    Generated_RMSE_extra(ii) = noise_diagnostics.generated_RMSE;
    Generated_bias_extra(ii) = noise_diagnostics.generated_bias;
    Generated_std_extra(ii) = noise_diagnostics.generated_std;

    fprintf(['[k extension] %d/%d | k = %d | T1 = %.3f, T2 = %.3f | ' ...
        'decision loss = %.6f USD/pack | generated RMSE = %.6f\n'], ...
        ii, num_k_extra, k_current, ...
        Q.T1_star, Q.T2_star, ...
        Q.O_per_pack + Q.A_per_pack + Q.R_per_pack, ...
        noise_diagnostics.generated_RMSE);
end

extension_elapsed_seconds = toc(extension_timer);

fprintf('>>> k = 51-2000 extension completed in %.2f minutes.\n', ...
    extension_elapsed_seconds / 60);

ExtraK_Table = table( ...
    k_vals_extra, ...
    P_extra.rmse_target, ...
    Generated_RMSE_extra, ...
    Generated_bias_extra, ...
    Generated_std_extra, ...
    P_extra.w_curve, ...
    P_extra.muM_curve, ...
    P_extra.sigM_curve, ...
    P_extra.muR_curve, ...
    P_extra.sigR_curve, ...
    T1_extra, ...
    T2_extra, ...
    O_extra, ...
    A_extra, ...
    R_extra, ...
    O_extra + A_extra + R_extra, ...
    Pred_Action_Ratio_extra(:, 1), ...
    Pred_Action_Ratio_extra(:, 2), ...
    Pred_Action_Ratio_extra(:, 3), ...
    A1_value_extra, ...
    A2_value_extra, ...
    A3_value_extra, ...
    'VariableNames', { ...
    'k', ...
    'RMSE_target', ...
    'RMSE_generated', ...
    'Residual_bias_generated', ...
    'Residual_std_generated', ...
    'w_right_component', ...
    'mu_main', ...
    'sigma_main', ...
    'mu_right', ...
    'sigma_right', ...
    'T1_star', ...
    'T2_star', ...
    'Opportunity_loss_per_pack_USD', ...
    'Mismatch_handling_rework_per_pack_USD', ...
    'Risk_penalty_per_pack_USD', ...
    'Decision_loss_per_pack_USD', ...
    'A1_direct_reuse_share', ...
    'A2_refurbishment_share', ...
    'A3_recycling_share', ...
    'A1_direct_reuse_value_per_pack_USD', ...
    'A2_refurbishment_value_per_pack_USD', ...
    'A3_recycling_value_per_pack_USD'});

writetable(ExtraK_Table, cfg.extra_k_csv);

% Combine the established k = 5-50 results and new k = 51-2000 results.
O_base = [O_base_existing; O_extra];
A_base = [A_base_existing; A_extra];
R_base = [R_base_existing; R_extra];

T1_base = [T1_base_existing; T1_extra];
T2_base = [T2_base_existing; T2_extra];

Pred_Action_Ratio_byK = [ ...
    Pred_Action_Ratio_existing; ...
    Pred_Action_Ratio_extra];

A1_value_perpack_byK = [A1_value_existing; A1_value_extra];
A2_value_perpack_byK = [A2_value_existing; A2_value_extra];
A3_value_perpack_byK = [A3_value_existing; A3_value_extra];

num_k = numel(k_vals);

if any([ ...
        numel(O_base), numel(A_base), numel(R_base), ...
        numel(T1_base), numel(T2_base), ...
        numel(A1_value_perpack_byK), ...
        numel(A2_value_perpack_byK), ...
        numel(A3_value_perpack_byK)] ~= num_k)
    error('The combined k = 5-2000 arrays have inconsistent lengths.');
end

if ~isequal(size(Pred_Action_Ratio_byK), [num_k, 3])
    error('The combined action-ratio matrix has an inconsistent size.');
end

fprintf('>>> Combined optimization range: k = %d to %d (%d values)\n', ...
    min(k_vals), max(k_vals), num_k);

%% 5) Construct market-scale cost matrices
scale_ton_list = double(cfg.scale_ton_list(:));
N_list = max(1, round(scale_ton_list .* 1000 ./ cfg.M_pack_kg));

num_N = numel(N_list);

O_Cost_NK = N_list .* O_base.';
A_Cost_NK = N_list .* A_base.';
R_Cost_NK = N_list .* R_base.';

Model_Cost_NK = repmat( ...
 (k_vals .* c_pack + c_train).', ...
 num_N, 1);

Total_Cost_NK = ...
 O_Cost_NK + ...
 A_Cost_NK + ...
 R_Cost_NK + ...
 Model_Cost_NK;

[opt_total_cost_of_N, opt_idx_of_N] = min(Total_Cost_NK, [], 2);

opt_k_of_N = k_vals(opt_idx_of_N);
opt_T1_of_N = T1_base(opt_idx_of_N);
opt_T2_of_N = T2_base(opt_idx_of_N);

row_idx = (1:num_N).';

opt_O_of_N = O_Cost_NK(sub2ind( ...
 size(O_Cost_NK), row_idx, opt_idx_of_N));

opt_A_of_N = A_Cost_NK(sub2ind( ...
 size(A_Cost_NK), row_idx, opt_idx_of_N));

opt_R_of_N = R_Cost_NK(sub2ind( ...
 size(R_Cost_NK), row_idx, opt_idx_of_N));

opt_Model_of_N = Model_Cost_NK(sub2ind( ...
 size(Model_Cost_NK), row_idx, opt_idx_of_N));

opt_Action_Ratio_of_N = Pred_Action_Ratio_byK(opt_idx_of_N, :);

opt_A1_value_of_N = ...
 N_list .* A1_value_perpack_byK(opt_idx_of_N);

opt_A2_value_of_N = ...
 N_list .* A2_value_perpack_byK(opt_idx_of_N);

opt_A3_value_of_N = ...
 N_list .* A3_value_perpack_byK(opt_idx_of_N);

opt_cost_per_pack_of_N = ...
 opt_total_cost_of_N ./ N_list;

%% 6) Compute gross and net realized pathway value
% The action-value components are already evaluated under the executed
% predicted action. Therefore, opportunity loss must not be subtracted a
% second time from the executed pathway value.

Gross_executed_action_value_of_N = ...
 opt_A1_value_of_N + ...
 opt_A2_value_of_N + ...
 opt_A3_value_of_N;

Net_realized_value_of_N = ...
 Gross_executed_action_value_of_N ...
 - opt_A_of_N ...
 - opt_R_of_N ...
 - opt_Model_of_N;

% Equivalent perfect-information accounting.
gross_executed_per_pack_by_k = ...
 A1_value_perpack_byK + ...
 A2_value_perpack_byK + ...
 A3_value_perpack_byK;

