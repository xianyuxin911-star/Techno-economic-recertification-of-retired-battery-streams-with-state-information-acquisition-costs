%% P2_Exp11_FineTuneDepth_Ablation.m
% Purpose:
% This script evaluates how the number of trainable encoder layers affects target-domain pack-level SOH prediction.
% It compares Last1Conv and Last2Conv fine-tuning under the same soft-min attention, MLP head, L2-SP regularization, and leave-one-pack-out cross-validation framework.
% Frozen-encoder and Last3Conv results are taken from P2_Exp03 and P2_Exp05, respectively, when constructing the full fine-tuning-depth comparison.

clc; clear; close all;

%% Configuration
rng(42);
cfg = struct();
script_dir = fileparts(mfilename('fullpath'));

if isempty(script_dir), script_dir = pwd; end
part_dir = script_dir;
output_results_dir = fullfile(part_dir, 'Output', 'Results');

if ~exist(output_results_dir, 'dir')
    mkdir(output_results_dir);
end

% Target-domain pack dataset generated by P2_Exp01.
cfg.target_mat = fullfile(part_dir, 'Output', 'Data', 'P2_Exp01_TargetPack_Input.mat');

% Source-domain CNN model generated by P1_Exp02.
cfg.source_mat = fullfile(part_dir, '..', 'Part1_Source-domain_Prediction', 'Output','Models', 'P1_SourceDomain_CNN_Model.mat');

% Output files generated by P2_Exp11.
cfg.metrics_csv = fullfile(output_results_dir, 'P2_Exp11_FineTuneDepthAblation_Metrics.csv');
cfg.soh_csv = fullfile(output_results_dir, 'P2_Exp11_FineTuneDepthAblation_SOHPredictions.csv');
cfg.result_mat = fullfile(output_results_dir, 'P2_Exp11_FineTuneDepthAblation_Results.mat');

% Input dimensions and network architecture.
cfg.M = 512;
cfg.nCell = 45;
cfg.dEmbed = 64;
cfg.dAttH  = 32;
cfg.dHeadH = 16;

% Training settings.
cfg.maxEpochs = 60;
cfg.warmupEpochs = 20;
cfg.lr_head = 1e-3;
cfg.lr_enc  = 1e-5;
cfg.beta1 = 0.9;
cfg.beta2 = 0.999;

% Softmin attention and regularization settings.
cfg.attn_tau  = 0.1;
cfg.l2_head   = 1e-4;
cfg.lambda_sp = 0.005;
cfg.clip_enc  = 1.0;
cfg.clip_head = 1.0;

% Fine-tuning-depth settings for this ablation experiment.
tune_depth_list = [1, 2];
depth_names = {'Last1Conv', 'Last2Conv'};
num_depths = numel(tune_depth_list);

fprintf('Target-domain data file:  %s\n', cfg.target_mat);
fprintf('Source-domain model file: %s\n', cfg.source_mat);

%% 1) Load target-domain data
if ~exist(cfg.target_mat, 'file')
    error('Target-domain data file not found: %s', cfg.target_mat);
end

T = load(cfg.target_mat);

X_img = single(T.X_img);       % [45, M, 1, Npack]
y_all = single(T.y_all(:));
Npack = size(X_img, 4);

if isfield(T, 'SN_List')
    SN_List = T.SN_List(:);
else
    SN_List = strings(numel(y_all), 1);
    for i = 1:numel(y_all)
        SN_List(i) = sprintf('Pack_%d', i);
    end
end

if isfield(T, 'cfg')
    cfgT = T.cfg;
else
    cfgT = struct();
end

fprintf('[Target] Dataset loaded successfully | Npack = %d\n', Npack);

%% 2) Load source-domain model and normalize target inputs
if ~exist(cfg.source_mat, 'file')
    error('Source-domain model file not found: %s', cfg.source_mat);
end

S = load(cfg.source_mat);

netFull = S.netFull;
muX = double(S.muX_full);
sgX = double(S.sgX_full);

if sgX < 1e-12
    sgX = 1;
end

% Normalize target-domain voltage curves using source-domain statistics.
X_img = (X_img - muX) / sgX;

%% 3) Prepare encoder and L2-SP anchor
layers = netFull.Layers;
idx_gap = find(arrayfun(@(L) strcmp(L.Name, 'gap'), layers), 1, 'first');

if isempty(idx_gap)
    error('The source-domain network does not contain a layer named gap. Please check netFull.Layers.');
end

lgraphEnc = layerGraph(layers(1:idx_gap));
enc_ref = dlnetwork(lgraphEnc);

fprintf('[INFO] Starting encoder fine-tuning depth ablation: Last1Conv vs Last2Conv\n');
fprintf('       Framework: Softmin attention + MLP head + L2-SP + LOOCV\n');

%% 4) Run experiments with different fine-tuning depths
Results = struct();

for dd = 1:num_depths

    depth_name = depth_names{dd};
    cfg.tune_last_conv = tune_depth_list(dd);

    fprintf('\n======================================================\n');
    fprintf('Running [%s]: fine-tuning the last %d convolutional layer(s)\n', ...
        depth_name, cfg.tune_last_conv);
    fprintf('======================================================\n');

    rng(42);

    yhat = nan(Npack, 1, 'single');
    maxAttn = nan(Npack, 1, 'single');
    maxAttnCell = nan(Npack, 1, 'single');
    attnWeightsAll = nan(Npack, cfg.nCell, 'single');

    % Leave-one-pack-out cross-validation.
    for te = 1:Npack

        tr = setdiff(1:Npack, te);

        % Standardize labels using the training fold only.
        y_train_raw = y_all(tr);

        muY = mean(y_train_raw);
        sgY = std(y_train_raw);

        if sgY < 1e-12
            sgY = 1;
        end

        y_train_norm = (y_train_raw - muY) / sgY;

        % Reset encoder and head for the current fold.
        currEnc = dlnetwork(lgraphEnc);

        attW1 = dlarray(0.01 * randn(cfg.dAttH, cfg.dEmbed, 'single'));
        attb1 = dlarray(zeros(cfg.dAttH, 1, 'single'));
        attW2 = dlarray(0.01 * randn(1, cfg.dAttH, 'single'));
        attb2 = dlarray(zeros(1, 1, 'single'));

        headW1 = dlarray(0.01 * randn(cfg.dHeadH, cfg.dEmbed, 'single'));
        headb1 = dlarray(zeros(cfg.dHeadH, 1, 'single'));
        headW2 = dlarray(0.01 * randn(1, cfg.dHeadH, 'single'));
        headb2 = dlarray(zeros(1, 1, 'single'));

        avgG_enc = [];
        avgSqG_enc = [];
        avgG_head = cell(8, 1);
        avgSqG_head = cell(8, 1);
        iter = 0;

        % Training loop.
        for ep = 1:cfg.maxEpochs

            order = randperm(numel(tr));

            for ii = 1:numel(order)

                iter = iter + 1;
                idx_in_tr = order(ii);
                p = tr(idx_in_tr);

                raw_pack = X_img(:, :, 1, p);

                dlX = dlarray(reshape(raw_pack', [1 cfg.M 1 cfg.nCell]), 'SSCB');
                y_true = dlarray(y_train_norm(idx_in_tr));

                [~, grads] = dlfeval(@model_grads_softmin_l2sp, ...
                    dlX, y_true, currEnc, enc_ref, ...
                    attW1, attb1, attW2, attb2, ...
                    headW1, headb1, headW2, headb2, cfg);

                % Update attention module and MLP head.
                headParams = {attW1, attb1, attW2, attb2, ...
                    headW1, headb1, headW2, headb2};

                headGrads = {grads.attW1, grads.attb1, grads.attW2, grads.attb2, ...
                    grads.headW1, grads.headb1, grads.headW2, grads.headb2};

                if cfg.clip_head > 0
                    headGrads = local_clip_grads_cell(headGrads, cfg.clip_head);
                end

                if isempty(avgG_head{1})
                    for kk = 1:8
                        avgG_head{kk} = [];
                        avgSqG_head{kk} = [];
                    end
                end

                for kk = 1:8
                    [headParams{kk}, avgG_head{kk}, avgSqG_head{kk}] = adamupdate( ...
                        headParams{kk}, headGrads{kk}, avgG_head{kk}, avgSqG_head{kk}, ...
                        iter, cfg.lr_head, cfg.beta1, cfg.beta2);
                end

                [attW1, attb1, attW2, attb2, ...
                    headW1, headb1, headW2, headb2] = headParams{:};

                % Update the last k convolutional layers of the encoder after warm-up.
                if ep > cfg.warmupEpochs

                    genc = grads.enc;
                    genc = local_mask_enc_grads_keep_last_conv(currEnc, genc, cfg.tune_last_conv);

                    if cfg.clip_enc > 0
                        genc = local_clip_grads_table(genc, cfg.clip_enc);
                    end

                    [currEnc, avgG_enc, avgSqG_enc] = adamupdate( ...
                        currEnc, genc, avgG_enc, avgSqG_enc, ...
                        iter, cfg.lr_enc, cfg.beta1, cfg.beta2);
                end
            end
        end

        % Inference on the held-out fold.
        raw_te = X_img(:, :, 1, te);

        dlX_te = dlarray(reshape(raw_te', [1 cfg.M 1 cfg.nCell]), 'SSCB');

        dlZ_te = predict(currEnc, dlX_te);
        Z_te = stripdims(squeeze(dlZ_te));

        [ypred_norm, w_te] = model_predict_softmin( ...
            Z_te, attW1, attb1, attW2, attb2, ...
            headW1, headb1, headW2, headb2, cfg);

        val_norm = gather(extractdata(ypred_norm));
        yhat(te) = val_norm * sgY + muY;

        w_num = double(gather(extractdata(w_te)));
        w_num = reshape(w_num, 1, []);

        attnWeightsAll(te, :) = single(w_num);

        [max_w, max_idx] = max(w_num);
        maxAttn(te) = single(max_w);
        maxAttnCell(te) = single(max_idx);

        fprintf('  [%s | Fold %02d/%02d] True=%.4f | Pred=%.4f | Error=%.4f | MaxAttn=%.2f\n', ...
            depth_name, te, Npack, y_all(te), yhat(te), ...
            yhat(te) - y_all(te), maxAttn(te));
    end

    % Evaluate the current fine-tuning-depth setting.
    err = yhat - y_all;

    RMSE = sqrt(mean(err.^2));
    MAE  = mean(abs(err));
    MAPE = mean(abs(err ./ y_all));

    sst = sum((y_all - mean(y_all)).^2);
    sse = sum(err.^2);
    R2  = 1 - sse / sst;

    fprintf('[%s] Final: R2=%.4f | RMSE=%.4f | MAE=%.4f | MAPE=%.4f\n', ...
        depth_name, R2, RMSE, MAE, MAPE);

    Results.(depth_name).yhat = yhat;
    Results.(depth_name).err = err;
    Results.(depth_name).abs_err = abs(err);
    Results.(depth_name).ape = abs(err ./ y_all);
    Results.(depth_name).maxAttn = maxAttn;
    Results.(depth_name).maxAttnCell = maxAttnCell;
    Results.(depth_name).attnWeightsAll = attnWeightsAll;
    Results.(depth_name).R2 = R2;
    Results.(depth_name).RMSE = RMSE;
    Results.(depth_name).MAE = MAE;
    Results.(depth_name).MAPE = MAPE;
end

%% 5) Summarize and export ablation results
fprintf('\nExporting encoder fine-tuning depth ablation results...\n');

% Summary metric table.
Metrics_Table = table();

for dd = 1:num_depths

    depth_name = depth_names{dd};

    tmp = table( ...
        {depth_name}, ...
        tune_depth_list(dd), ...
        double(Results.(depth_name).R2), ...
        double(Results.(depth_name).RMSE), ...
        double(Results.(depth_name).MAE), ...
        double(Results.(depth_name).MAPE), ...
        'VariableNames', { ...
        'Setting', ...
        'Trainable_Last_Conv_Layers', ...
        'R2', ...
        'RMSE', ...
        'MAE', ...
        'MAPE'});

    Metrics_Table = [Metrics_Table; tmp];
end

writetable(Metrics_Table, cfg.metrics_csv);

% Pack-level prediction table.
SOH_Table = table( ...
    (1:Npack)', ...
    SN_List, ...
    double(y_all(:)), ...
    'VariableNames', { ...
    'Pack_Index', ...
    'Pack_ID', ...
    'True_SOH'});

for dd = 1:num_depths

    depth_name = depth_names{dd};

    SOH_Table.(['Pred_' depth_name]) = double(Results.(depth_name).yhat(:));
    SOH_Table.(['Error_' depth_name]) = double(Results.(depth_name).err(:));
    SOH_Table.(['AbsError_' depth_name]) = double(Results.(depth_name).abs_err(:));
    SOH_Table.(['APE_' depth_name]) = double(Results.(depth_name).ape(:));
    SOH_Table.(['MaxAttn_' depth_name]) = double(Results.(depth_name).maxAttn(:));
    SOH_Table.(['MaxAttnCell_' depth_name]) = double(Results.(depth_name).maxAttnCell(:));
end

writetable(SOH_Table, cfg.soh_csv);

% Export attention-weight matrix for each fine-tuning-depth setting.
for dd = 1:num_depths

    depth_name = depth_names{dd};

    attn_mat = double(Results.(depth_name).attnWeightsAll);  % Npack x nCell

    attn_col_names = arrayfun(@(x) sprintf('Cell_%02d', x), ...
        1:cfg.nCell, 'UniformOutput', false);

    Attn_Table = array2table(attn_mat, ...
        'VariableNames', attn_col_names);

    Attn_Table = [ ...
        table((1:Npack)', SN_List, ...
        'VariableNames', {'Pack_Index', 'Pack_ID'}), ...
        Attn_Table];

 attn_csv = fullfile(output_results_dir, sprintf('P2_Exp11_FineTuneDepthAblation_%s_Attention.csv', depth_name));

    writetable(Attn_Table, attn_csv);

    Results.(depth_name).Attention_Table = Attn_Table;
    Results.(depth_name).Attention_CSV = attn_csv;
end

% Save complete MATLAB result file for downstream analysis.
save(cfg.result_mat, ...
    'cfg', ...
    'cfgT', ...
    'SN_List', ...
    'y_all', ...
    'tune_depth_list', ...
    'depth_names', ...
    'Results', ...
    'Metrics_Table', ...
    'SOH_Table');

fprintf('[Export] Export completed:\n');
fprintf('  - Metrics table:       %s\n', cfg.metrics_csv);
fprintf('  - SOH prediction table:%s\n', cfg.soh_csv);

for dd = 1:num_depths
    depth_name = depth_names{dd};
    fprintf('  - Attention table:     %s\n', Results.(depth_name).Attention_CSV);
end

fprintf('  - MATLAB result file:  %s\n', cfg.result_mat);


%% Local helper functions
function [loss, grads] = model_grads_softmin_l2sp(X, y, net, net_ref, ...
    attW1, attb1, attW2, attb2, headW1, headb1, headW2, headb2, cfg)

    [Z_raw, ~] = predict(net, X);
    Z = stripdims(squeeze(Z_raw));

    [ypred, ~] = model_predict_softmin( ...
        Z, attW1, attb1, attW2, attb2, ...
        headW1, headb1, headW2, headb2, cfg);

    mse_loss = mean((ypred - y).^2);

    l2_head_term = sum(attW1(:).^2) + ...
                   sum(attW2(:).^2) + ...
                   sum(headW1(:).^2) + ...
                   sum(headW2(:).^2);

    % Encoder L2-SP regularization toward the source-domain encoder.
    l2_sp_term = 0;

    if cfg.lambda_sp > 0

        T_curr = net.Learnables;
        T_ref  = net_ref.Learnables;

        for ii = 1:height(T_curr)
            diff = T_curr.Value{ii} - T_ref.Value{ii};
            l2_sp_term = l2_sp_term + sum(diff(:).^2);
        end
    end

    loss = mse_loss + cfg.l2_head * l2_head_term + cfg.lambda_sp * l2_sp_term;

    all_grads = dlgradient(loss, ...
        {net.Learnables, attW1, attb1, attW2, attb2, headW1, headb1, headW2, headb2});

    grads.enc    = all_grads{1};
    grads.attW1  = all_grads{2};
    grads.attb1  = all_grads{3};
    grads.attW2  = all_grads{4};
    grads.attb2  = all_grads{5};
    grads.headW1 = all_grads{6};
    grads.headb1 = all_grads{7};
    grads.headW2 = all_grads{8};
    grads.headb2 = all_grads{9};
end

function [ypred, w] = model_predict_softmin(Z, ...
    attW1, attb1, attW2, attb2, headW1, headb1, headW2, headb2, cfg)

    % Soft-min attention assigns larger weights to cells with lower learned scores.
    H = relu(attW1 * Z + attb1);

    score = -(attW2 * H + attb2) / cfg.attn_tau;
    score = score - max(score, [], 2);

    w = exp(score) ./ sum(exp(score), 2);

    % Aggregate cell embeddings into a pack-level embedding.
    zbag = Z * w.';

    h1 = relu(headW1 * zbag + headb1);
    ypred = headW2 * h1 + headb2;
end

function genc = local_mask_enc_grads_keep_last_conv(net, genc, keep_last_conv)

    L = net.Layers;
    layerNames = string({L.Name});
    isConv = false(size(L));

    for ii = 1:numel(L)

        cls = class(L(ii));

        if contains(cls, 'Convolution', 'IgnoreCase', true) || ...
                contains(L(ii).Name, 'conv', 'IgnoreCase', true)
            isConv(ii) = true;
        end
    end

    convIdx = find(isConv);

    if isempty(convIdx)
        learnLayer = string(genc.Layer);
        uniq = unique(learnLayer, 'stable');
        keep = uniq(max(1, end - 4):end);
    else
        kk = min(keep_last_conv, numel(convIdx));
        keep = layerNames(convIdx(end - kk + 1:end));
    end

    for ii = 1:height(genc)

        if ~ismember(string(genc.Layer(ii)), keep)

            gi = genc.Value{ii};

            if isempty(gi)
                continue;
            end

            gi_data = extractdata(gi);
            genc.Value{ii} = dlarray(zeros(size(gi_data), 'like', gi_data));
        end
    end
end

function gtab = local_clip_grads_table(gtab, clipnorm)

    ss = 0;

    for ii = 1:height(gtab)

        gi = gtab.Value{ii};

        if isempty(gi)
            continue;
        end

        gi_data = extractdata(gi);
        ss = ss + sum(gi_data(:).^2);
    end

    gn = sqrt(ss + 1e-12);

    if gn > clipnorm

        scale = clipnorm / gn;

        for ii = 1:height(gtab)

            gi = gtab.Value{ii};

            if isempty(gi)
                continue;
            end

            gtab.Value{ii} = gi * scale;
        end
    end
end

function gcell = local_clip_grads_cell(gcell, clipnorm)

    ss = 0;

    for ii = 1:numel(gcell)

        gi = gcell{ii};

        if isempty(gi)
            continue;
        end

        gi_data = extractdata(gi);
        ss = ss + sum(gi_data(:).^2);
    end

    gn = sqrt(ss + 1e-12);

    if gn > clipnorm

        scale = clipnorm / gn;

        for ii = 1:numel(gcell)

            if isempty(gcell{ii})
                continue;
            end

            gcell{ii} = gcell{ii} * scale;
        end
    end
end