%% Sup01_regression_head.m
% Purpose:
% This supplementary control experiment compares different regression heads
% on fixed CNN mean-pooled pack-level features.
%
% The source-pretrained CNN encoder is kept frozen. Cell-level embeddings are
% extracted for each target-domain pack and aggregated by mean pooling across
% the 45 cells. The resulting fixed pack-level features are then used to
% compare MLP, Ridge regression, Gaussian process regression, and support
% vector regression under leave-one-pack-out cross-validation.
%
% This experiment is a fixed-feature regression-head control and does not
% replace the main L2-SP + CNN-attention + MLP model.
%
% Inputs:
% Output/Result01_TargetPack_Partial.mat
% ../Part1_Source-domain_Prediction/Output/cellSOH_sourceModel_Partial.mat
%
% Outputs:
% Output/Sup01_FixedFeature_HeadPredictions.csv
% Output/Sup01_FixedFeature_HeadMetrics.csv
% Output/Sup01_FixedFeature_PackFeatures.csv
% Output/Sup01_FixedFeature_HeadResults.mat

clc; clear; close all;

%% Configuration
rng(42, 'twister');

cfg = struct();

% Target-domain pack dataset generated by Main01 in Part2.
cfg.target_mat = fullfile('Output', 'Result01_TargetPack_Partial.mat');

% Source-domain CNN model generated in Part1.
cfg.source_mat = fullfile('..', ...
    'Part1_Source-domain_Prediction', ...
    'Output', ...
    'cellSOH_sourceModel_Partial.mat');

% Output files generated by Sup01.
cfg.pred_csv     = fullfile('Output', 'Sup01_FixedFeature_HeadPredictions.csv');
cfg.metrics_csv  = fullfile('Output', 'Sup01_FixedFeature_HeadMetrics.csv');
cfg.features_csv = fullfile('Output', 'Sup01_FixedFeature_PackFeatures.csv');
cfg.result_mat   = fullfile('Output', 'Sup01_FixedFeature_HeadResults.mat');

% Input dimensions.
cfg.M = 512;
cfg.nCell = 45;
cfg.dEmbed = 64;

% MLP regression-head settings.
% Only the MLP head is trained; the CNN encoder is not updated.
cfg.dHeadH = 16;
cfg.maxEpochsHead = 300;
cfg.lr_mlp = 1e-3;
cfg.beta1 = 0.9;
cfg.beta2 = 0.999;
cfg.l2_mlp = 1e-4;
cfg.clip_mlp = 1.0;

% Conventional regression-head settings.
cfg.lambda_ridge = 1e-3;
cfg.svm_box = 10;

if ~exist('Output', 'dir')
    mkdir('Output');
end

fprintf('================ Sup01 fixed-feature regression-head control ================\n');
fprintf('Target-domain data file:  %s\n', cfg.target_mat);
fprintf('Source-domain model file: %s\n', cfg.source_mat);

%% 1) Load target-domain data and source-domain model
if ~exist(cfg.target_mat, 'file')
    error('Target-domain data not found: %s', cfg.target_mat);
end

if ~exist(cfg.source_mat, 'file')
    error('Source-domain model not found: %s', cfg.source_mat);
end

T = load(cfg.target_mat);

X_img = single(T.X_img);      % [45, M, 1, Npack]
y_all = single(T.y_all(:));
Npack = size(X_img, 4);

if isfield(T, 'SN_List')
    SN_List = T.SN_List(:);
else
    SN_List = strings(numel(y_all), 1);
    for i = 1:numel(y_all)
        SN_List(i) = sprintf('Pack_%d', i);
    end
end

if isfield(T, 'cfg')
    cfgT = T.cfg;
else
    cfgT = struct();
end

S = load(cfg.source_mat);

netFull = S.netFull;
muX = double(S.muX_full);
sgX = double(S.sgX_full);

if sgX < 1e-12
    sgX = 1;
end

fprintf('[Target] Dataset loaded successfully | Npack = %d\n', Npack);

%% 2) Normalize target-domain voltage curves using source-domain statistics
X_img = (X_img - muX) / sgX;

%% 3) Prepare the frozen source-pretrained CNN encoder
layers = netFull.Layers;
idx_gap = find(arrayfun(@(L) strcmp(L.Name, 'gap'), layers), 1, 'first');

if isempty(idx_gap)
    error('The layer named "gap" was not found in netFull.Layers.');
end

lgraphEnc = layerGraph(layers(1:idx_gap));
fixedEnc = dlnetwork(lgraphEnc);

%% 4) Extract fixed pack-level features
% No target-domain SOH labels are used in this feature-extraction step.
% For each pack, the CNN encoder produces a 64 x 45 cell-level embedding
% matrix. Mean pooling across the 45 cells is then used to construct one
% fixed pack-level feature vector.

Fixed_Feats = zeros(Npack, cfg.dEmbed, 'double');

fprintf('[Feature] Extracting fixed CNN mean-pooled pack features...\n');

for p = 1:Npack

    raw_pack = X_img(:, :, 1, p);

    dlX = dlarray(reshape(raw_pack', [1 cfg.M 1 cfg.nCell]), 'SSCB');

    dlZ = predict(fixedEnc, dlX);
    Z = stripdims(squeeze(dlZ));             % [dEmbed, nCell]
    Z_num = double(gather(extractdata(Z)));  % [dEmbed, nCell]

    zbag = mean(Z_num, 2);                   % [dEmbed, 1]
    Fixed_Feats(p, :) = zbag(:)';
end

fprintf('[Feature] Fixed feature extraction completed.\n');

%% 5) Leave-one-pack-out regression-head comparison
yhat_MLP   = nan(Npack, 1, 'single');
yhat_Ridge = nan(Npack, 1, 'single');
yhat_GPR   = nan(Npack, 1, 'single');
yhat_SVM   = nan(Npack, 1, 'single');

for te = 1:Npack

    tr = setdiff(1:Npack, te);

    % Standardize labels using only the training packs in the current fold.
    y_train_raw = y_all(tr);

    muY = mean(y_train_raw);
    sgY = std(y_train_raw);

    if sgY < 1e-12
        sgY = 1;
    end

    y_train_norm = double((y_train_raw - muY) / sgY);

    % Standardize features using only the training packs in the current fold.
    Feats_Train = Fixed_Feats(tr, :);
    Feats_Test  = Fixed_Feats(te, :);

    muF = mean(Feats_Train, 1);
    sgF = std(Feats_Train, [], 1);
    sgF(sgF < 1e-12) = 1;

    Feats_Train_std = (Feats_Train - muF) ./ sgF;
    Feats_Test_std  = (Feats_Test  - muF) ./ sgF;

    %% 5.1 MLP regression head
    rng(42 + te, 'twister');

    headW1 = dlarray(0.01 * randn(cfg.dHeadH, cfg.dEmbed, 'single'));
    headb1 = dlarray(zeros(cfg.dHeadH, 1, 'single'));
    headW2 = dlarray(0.01 * randn(1, cfg.dHeadH, 'single'));
    headb2 = dlarray(zeros(1, 1, 'single'));

    avgG_mlp = cell(4, 1);
    avgSqG_mlp = cell(4, 1);
    iter = 0;

    for ep = 1:cfg.maxEpochsHead

        order = randperm(numel(tr));

        for ii = 1:numel(order)

            iter = iter + 1;
            idx = order(ii);

            x = dlarray(single(Feats_Train_std(idx, :)'));
            y = dlarray(single(y_train_norm(idx)));

            [~, grads] = dlfeval(@mlp_grads, ...
                x, y, headW1, headb1, headW2, headb2, cfg);

            mlpParams = {headW1, headb1, headW2, headb2};
            mlpGrads  = {grads.headW1, grads.headb1, grads.headW2, grads.headb2};

            if cfg.clip_mlp > 0
                mlpGrads = local_clip_grads_cell(mlpGrads, cfg.clip_mlp);
            end

            for k = 1:4
                [mlpParams{k}, avgG_mlp{k}, avgSqG_mlp{k}] = adamupdate( ...
                    mlpParams{k}, mlpGrads{k}, avgG_mlp{k}, avgSqG_mlp{k}, ...
                    iter, cfg.lr_mlp, cfg.beta1, cfg.beta2);
            end

            [headW1, headb1, headW2, headb2] = mlpParams{:};
        end
    end

    xte = dlarray(single(Feats_Test_std'));
    ypred_mlp_norm = mlp_predict(xte, headW1, headb1, headW2, headb2);
    yhat_MLP(te) = double(extractdata(ypred_mlp_norm)) * sgY + muY;

    %% 5.2 Ridge regression head
    try
        Mdl_Ridge = fitrlinear( ...
            Feats_Train_std, ...
            y_train_norm, ...
            'Learner', 'leastsquares', ...
            'Regularization', 'ridge', ...
            'Lambda', cfg.lambda_ridge);

        pred_ridge_norm = predict(Mdl_Ridge, Feats_Test_std);
        yhat_Ridge(te) = pred_ridge_norm * sgY + muY;

    catch ME
        warning('Ridge failed at Fold %d: %s', te, ME.message);
        yhat_Ridge(te) = NaN;
    end

    %% 5.3 Gaussian process regression head
    try
        Mdl_GPR = fitrgp( ...
            Feats_Train_std, ...
            y_train_norm, ...
            'KernelFunction', 'squaredexponential', ...
            'Standardize', false);

        pred_gpr_norm = predict(Mdl_GPR, Feats_Test_std);
        yhat_GPR(te) = pred_gpr_norm * sgY + muY;

    catch ME
        warning('GPR failed at Fold %d: %s', te, ME.message);
        yhat_GPR(te) = NaN;
    end

    %% 5.4 Support vector regression head
    try
        Mdl_SVM = fitrsvm( ...
            Feats_Train_std, ...
            y_train_norm, ...
            'KernelFunction', 'gaussian', ...
            'Standardize', false, ...
            'BoxConstraint', cfg.svm_box);

        pred_svm_norm = predict(Mdl_SVM, Feats_Test_std);
        yhat_SVM(te) = pred_svm_norm * sgY + muY;

    catch ME
        warning('SVM failed at Fold %d: %s', te, ME.message);
        yhat_SVM(te) = NaN;
    end

    fprintf('[Fold %02d/%02d] True=%.4f | MLP=%.4f | Ridge=%.4f | GPR=%.4f | SVM=%.4f\n', ...
        te, Npack, y_all(te), yhat_MLP(te), yhat_Ridge(te), yhat_GPR(te), yhat_SVM(te));
end

%% 6) Evaluate regression-head performance
Models = {'MLP'; 'Ridge'; 'GPR'; 'SVM'};
Preds = {yhat_MLP; yhat_Ridge; yhat_GPR; yhat_SVM};

R2_all = nan(numel(Models), 1);
RMSE_all = nan(numel(Models), 1);
MAE_all = nan(numel(Models), 1);
MAPE_all = nan(numel(Models), 1);
N_valid_all = nan(numel(Models), 1);

fprintf('\n==================== Fixed-feature regression-head comparison ====================\n');
fprintf('%-10s | %-8s | %-8s | %-8s | %-10s | %-7s\n', ...
    'Model', 'R2', 'RMSE', 'MAE', 'MAPE(%)', 'Nvalid');
fprintf('-------------------------------------------------------------------------------\n');

for i = 1:numel(Models)

    yh = Preds{i};
    valid = ~isnan(yh) & ~isnan(y_all);

    N_valid_all(i) = sum(valid);

    err = yh(valid) - y_all(valid);

    RMSE_all(i) = sqrt(mean(err.^2));
    MAE_all(i) = mean(abs(err));
    MAPE_all(i) = mean(abs(err ./ y_all(valid))) * 100;

    sst = sum((y_all(valid) - mean(y_all(valid))).^2);
    sse = sum(err.^2);

    R2_all(i) = 1 - sse / sst;

    fprintf('%-10s | %.4f   | %.4f   | %.4f   | %.4f     | %d\n', ...
        Models{i}, R2_all(i), RMSE_all(i), MAE_all(i), MAPE_all(i), N_valid_all(i));
end

%% 7) Export fixed-feature regression-head results
fprintf('\nExporting fixed-feature regression-head results...\n');

Export_SOH = table( ...
    (1:Npack)', ...
    SN_List, ...
    double(y_all(:)), ...
    double(yhat_MLP(:)), ...
    double(yhat_Ridge(:)), ...
    double(yhat_GPR(:)), ...
    double(yhat_SVM(:)), ...
    'VariableNames', { ...
    'Pack_Index', ...
    'Pack_ID', ...
    'True_SOH', ...
    'Pred_MLP', ...
    'Pred_Ridge', ...
    'Pred_GPR', ...
    'Pred_SVM'});

Export_Metrics = table( ...
    Models, ...
    double(R2_all), ...
    double(RMSE_all), ...
    double(MAE_all), ...
    double(MAPE_all), ...
    double(N_valid_all), ...
    'VariableNames', { ...
    'Model', ...
    'R2', ...
    'RMSE', ...
    'MAE', ...
    'MAPE_percent', ...
    'N_valid'});

feature_names = arrayfun(@(j) sprintf('Feat_%02d', j), ...
    1:cfg.dEmbed, 'UniformOutput', false);

Export_Features = array2table(Fixed_Feats, ...
    'VariableNames', feature_names);

Export_Features = [ ...
    table((1:Npack)', SN_List, double(y_all(:)), ...
    'VariableNames', {'Pack_Index', 'Pack_ID', 'True_SOH'}), ...
    Export_Features];

writetable(Export_SOH, cfg.pred_csv);
writetable(Export_Metrics, cfg.metrics_csv);
writetable(Export_Features, cfg.features_csv);

save(cfg.result_mat, ...
    'cfg', ...
    'cfgT', ...
    'SN_List', ...
    'X_img', ...
    'y_all', ...
    'Fixed_Feats', ...
    'Models', ...
    'Preds', ...
    'yhat_MLP', ...
    'yhat_Ridge', ...
    'yhat_GPR', ...
    'yhat_SVM', ...
    'R2_all', ...
    'RMSE_all', ...
    'MAE_all', ...
    'MAPE_all', ...
    'N_valid_all', ...
    'Export_SOH', ...
    'Export_Metrics', ...
    'Export_Features');

fprintf('[Export] Export completed.\n');
fprintf('  - Prediction results: %s\n', cfg.pred_csv);
fprintf('  - Performance metrics: %s\n', cfg.metrics_csv);
fprintf('  - Fixed features:      %s\n', cfg.features_csv);
fprintf('  - MATLAB result file:  %s\n', cfg.result_mat);

fprintf('\n================ Sup01 fixed-feature regression-head control completed ================\n');

%% Local helper functions
function [loss, grads] = mlp_grads(x, y, headW1, headb1, headW2, headb2, cfg)

    ypred = mlp_predict(x, headW1, headb1, headW2, headb2);

    mse_loss = mean((ypred - y).^2);
    l2_term = sum(headW1(:).^2) + sum(headW2(:).^2);

    loss = mse_loss + cfg.l2_mlp * l2_term;

    all_grads = dlgradient(loss, {headW1, headb1, headW2, headb2});

    grads.headW1 = all_grads{1};
    grads.headb1 = all_grads{2};
    grads.headW2 = all_grads{3};
    grads.headb2 = all_grads{4};
end

function ypred = mlp_predict(x, headW1, headb1, headW2, headb2)

    % Remove dimension labels to ensure valid matrix multiplication.
    x = stripdims(x);

    h1 = relu(headW1 * x + headb1);
    ypred = headW2 * h1 + headb2;
end

function gcell = local_clip_grads_cell(gcell, clipnorm)

    ss = 0;

    for i = 1:numel(gcell)

        gi = gcell{i};

        if isempty(gi)
            continue;
        end

        ss = ss + sum(extractdata(gi).^2, 'all');
    end

    gn = sqrt(ss + 1e-12);

    if gn > clipnorm

        scale = clipnorm / gn;

        for i = 1:numel(gcell)

            if isempty(gcell{i})
                continue;
            end

            gcell{i} = gcell{i} * scale;
        end
    end
end