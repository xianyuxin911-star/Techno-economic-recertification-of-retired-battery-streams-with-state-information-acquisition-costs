%% =========================================================================
% P4_Exp04_fixed_scale_LFP_k_regret_benchmark.m
%
% Purpose:
% This script quantifies the additional cost associated with fixing the
% number of SOH-labelled target packs, k, at a non-optimal value under the
% fixed-scale LFP recertification setting.
%
% For every evaluated k, the SOH decision thresholds T1 and T2 have already
% been re-optimized by Part4 Exp01. Therefore, this experiment isolates the
% regret associated with a non-adaptive label-count policy; it does not
% represent a policy with both k and the thresholds held fixed.
%
% Required upstream file:
% P4_Exp01_FixedScale_LFP_Results.mat
%
% Main outputs:
% 1) Full k-dependent additional-cost curve;
% 2) Representative fixed-k scenarios (k = 5, 25 and 50, plus k*);
% 3) Extended Data Fig. 6a.
%% =========================================================================

clear;
clc;
close all;

set(groot, 'DefaultFigureVisible', 'on');

%% 1) Path configuration
cfg = struct();

script_dir = fileparts(mfilename('fullpath'));

if isempty(script_dir)
 script_dir = pwd;
end

% Part4 folders.
cfg.output_dir = fullfile(script_dir, 'Output');
cfg.results_dir = fullfile(cfg.output_dir, 'Results');

cfg.figure_root = fullfile(script_dir, 'Figures');
cfg.main_fig_dir = fullfile(cfg.figure_root, 'Main');
cfg.extended_fig_dir = fullfile(cfg.figure_root, 'Extended');

% Primary input generated by the Part4 Exp01.
cfg.lfp_result_primary = fullfile(cfg.results_dir, ...
 'P4_Exp01_FixedScale_LFP_Results.mat');

% Legacy result fallback.
cfg.lfp_result_legacy = fullfile(cfg.output_dir, ...
 'Result01_FixedScale_LFP_Results.mat');

% Workspace fallbacks.
cfg.lfp_workspace_primary = fullfile(cfg.results_dir, ...
 'P4_Exp01_FixedScale_LFP_Workspace.mat');

cfg.lfp_workspace_legacy = fullfile(cfg.output_dir, ...
 'Result01_FixedScale_LFP_Workspace.mat');

% Output files.
cfg.result_mat = fullfile(cfg.results_dir, ...
 'P4_Exp04_FixedScale_LFP_KRegretBenchmark_Results.mat');

cfg.curve_csv = fullfile(cfg.results_dir, ...
 'P4_Exp04_FixedScale_LFP_KRegretBenchmark_Curve.csv');

cfg.scenario_csv = fullfile(cfg.results_dir, ...
 'P4_Exp04_FixedScale_LFP_KRegretBenchmark_Scenarios.csv');

% Extended Data Fig. 6a output.
cfg.fig_regret = fullfile(cfg.extended_fig_dir, ...
 'ExD06a_LFP_FixedScale_KRegretBenchmark.fig');

cfg.png_regret = fullfile(cfg.extended_fig_dir, ...
 'ExD06a_LFP_FixedScale_KRegretBenchmark.png');

cfg.png_resolution = 600;

% Representative fixed-k policies. Thresholds remain re-optimized at each k.
cfg.fixed_k_benchmarks = [5, 25, 50];

% Create required folders.
folder_list = { ...
 cfg.output_dir, ...
 cfg.results_dir, ...
 cfg.figure_root, ...
 cfg.main_fig_dir, ...
 cfg.extended_fig_dir};

for ii = 1:numel(folder_list)
 if ~exist(folder_list{ii}, 'dir')
 [status, message] = mkdir(folder_list{ii});

 if ~status
 error('Unable to create folder:\n%s\nReason: %s', ...
 folder_list{ii}, message);
 end
 end
end

%% 2) Locate the fixed-scale LFP result file
if isfile(cfg.lfp_result_primary)

 cfg.input_file = cfg.lfp_result_primary;
 cfg.input_type = 'structured_result';

elseif isfile(cfg.lfp_result_legacy)

 cfg.input_file = cfg.lfp_result_legacy;
 cfg.input_type = 'structured_result';

elseif isfile(cfg.lfp_workspace_primary)

 cfg.input_file = cfg.lfp_workspace_primary;
 cfg.input_type = 'workspace';

elseif isfile(cfg.lfp_workspace_legacy)

 cfg.input_file = cfg.lfp_workspace_legacy;
 cfg.input_type = 'workspace';

else

 error(['Fixed-scale LFP result or workspace file was not found.\n' ...
 'Checked:\n%s\n%s\n%s\n%s\n' ...
 'Run Part4 Exp01 before running this benchmark.'], ...
 cfg.lfp_result_primary, ...
 cfg.lfp_result_legacy, ...
 cfg.lfp_workspace_primary, ...
 cfg.lfp_workspace_legacy);
end

fprintf('================ Part4 Exp04: fixed-scale LFP k-regret benchmark ================\n');
fprintf('Input file: %s\n', cfg.input_file);
fprintf('Input type: %s\n', cfg.input_type);
fprintf('Curve CSV: %s\n', cfg.curve_csv);
fprintf('Scenario CSV: %s\n', cfg.scenario_csv);
fprintf('Result MAT: %s\n', cfg.result_mat);
fprintf('Extended figure output: %s\n', cfg.png_regret);

%% 3) Load and validate fixed-scale LFP optimization results
S = load(cfg.input_file);

if isfield(S, 'LFP_fixed_result')
 R = S.LFP_fixed_result;
else
 % Workspace-style fallback.
 R = S;
end

required_fields = { ...
 'Fixed_Scale_N', ...
 'k_vals', ...
 'Fixed_Total_Cost', ...
 'Fixed_O', ...
 'Fixed_R', ...
 'Fixed_Model', ...
 'T1_base', ...
 'T2_base'};

for ii = 1:numel(required_fields)

 field_now = required_fields{ii};

 if ~isfield(R, field_now)
 error('Required field is missing from the fixed-scale LFP result: %s', ...
 field_now);
 end
end

% The Exp01 uses Fixed_H for mismatch handling and rework.
% Fixed_A is accepted only as a legacy alias.
[Fixed_H, handling_field_used] = local_get_first_existing_field( ...
 R, {'Fixed_H', 'Fixed_A'}, ...
 'Mismatch handling and rework cost');

Fixed_Scale_N = double(R.Fixed_Scale_N);
k_vals = double(R.k_vals(:));

Fixed_Total_Cost = double(R.Fixed_Total_Cost(:));
Fixed_O = double(R.Fixed_O(:));
Fixed_H = double(Fixed_H(:));
Fixed_R = double(R.Fixed_R(:));
Fixed_Model = double(R.Fixed_Model(:));

T1_base = double(R.T1_base(:));
T2_base = double(R.T2_base(:));

num_k = numel(k_vals);

array_lengths = [ ...
 numel(Fixed_Total_Cost), ...
 numel(Fixed_O), ...
 numel(Fixed_H), ...
 numel(Fixed_R), ...
 numel(Fixed_Model), ...
 numel(T1_base), ...
 numel(T2_base)];

if any(array_lengths ~= num_k)
 error('The lengths of the loaded k-dependent arrays are inconsistent.');
end

if any(~isfinite(k_vals)) || any(~isfinite(Fixed_Total_Cost))
 error('The loaded k values or total-cost curve contains non-finite values.');
end

if any(diff(k_vals) <= 0)
 error('k_vals must be strictly increasing.');
end

if Fixed_Scale_N <= 0 || ~isfinite(Fixed_Scale_N)
 error('Fixed_Scale_N must be a positive finite value.');
end

% Verify the accounting identity from Exp01.
Fixed_Total_Cost_rebuilt = Fixed_O + Fixed_H + Fixed_R + Fixed_Model;
accounting_abs_error = abs(Fixed_Total_Cost - Fixed_Total_Cost_rebuilt);
accounting_tolerance = max(1e-6, ...
 1e-9 * max(max(abs(Fixed_Total_Cost)), 1));

if max(accounting_abs_error) > accounting_tolerance
 error(['The loaded cost components do not reproduce Fixed_Total_Cost.\n' ...
 'Maximum absolute discrepancy = %.6g USD.\n' ...
 'Rerun the Part4 Exp01 before running Exp04.'], ...
 max(accounting_abs_error));
end

% Recompute the fixed-scale optimum from the loaded cost curve to avoid
% relying on a stale saved index.
[C_star, opt_k_idx] = min(Fixed_Total_Cost);

opt_k = k_vals(opt_k_idx);
opt_T1 = T1_base(opt_k_idx);
opt_T2 = T2_base(opt_k_idx);
c_star_per_pack = C_star / Fixed_Scale_N;

fprintf('>>> Fixed-scale LFP result loaded successfully.\n');
fprintf('>>> Handling-cost field used: %s\n', handling_field_used);
fprintf('>>> Fixed scale: %.0f packs.\n', Fixed_Scale_N);
fprintf('>>> Evaluated k range: %d to %d.\n', min(k_vals), max(k_vals));
fprintf('>>> Optimal k* = %d, T1* = %.4f, T2* = %.4f.\n', ...
 opt_k, opt_T1, opt_T2);
fprintf('>>> Optimal total cost = %.4f USD.\n', C_star);
fprintf('>>> Optimal cost per pack = %.4f USD per pack.\n', ...
 c_star_per_pack);

%% 4) Compute the k-dependent additional-cost curve
Cost_per_pack_USD = Fixed_Total_Cost ./ Fixed_Scale_N;

Additional_cost_USD = Fixed_Total_Cost - C_star;
Additional_cost_per_pack_USD = Additional_cost_USD ./ Fixed_Scale_N;

% Percentage by which a fixed-k policy exceeds the globally optimized cost.
Excess_cost_relative_to_optimum_percent = ...
 100 .* Additional_cost_USD ./ max(C_star, eps);

% Percentage reduction obtained by moving from each fixed-k policy to k*.
Cost_reduction_from_fixed_policy_to_optimum_percent = ...
 100 .* Additional_cost_USD ./ max(Fixed_Total_Cost, eps);

% Numerical cleanup at the optimum.
Additional_cost_USD(abs(Additional_cost_USD) < 1e-9) = 0;
Additional_cost_per_pack_USD( ...
 abs(Additional_cost_per_pack_USD) < 1e-12) = 0;

Excess_cost_relative_to_optimum_percent( ...
 abs(Excess_cost_relative_to_optimum_percent) < 1e-10) = 0;

Cost_reduction_from_fixed_policy_to_optimum_percent( ...
 abs(Cost_reduction_from_fixed_policy_to_optimum_percent) < 1e-10) = 0;

Thresholds_reoptimized_given_k = true(num_k, 1);

KRegret_Curve_Table = table( ...
 k_vals, ...
 Thresholds_reoptimized_given_k, ...
 T1_base, ...
 T2_base, ...
 Fixed_Total_Cost, ...
 Cost_per_pack_USD, ...
 Fixed_O, ...
 Fixed_H, ...
 Fixed_R, ...
 Fixed_Model, ...
 Additional_cost_USD, ...
 Additional_cost_per_pack_USD, ...
 Excess_cost_relative_to_optimum_percent, ...
 Cost_reduction_from_fixed_policy_to_optimum_percent, ...
 accounting_abs_error, ...
 'VariableNames', { ...
 'k', ...
 'Thresholds_reoptimized_given_k', ...
 'T1_star_given_k', ...
 'T2_star_given_k', ...
 'Total_cost_USD', ...
 'Cost_per_pack_USD', ...
 'Opportunity_loss_USD', ...
 'Mismatch_handling_rework_cost_USD', ...
 'Risk_penalty_USD', ...
 'SOH_labelling_adaptation_cost_USD', ...
 'Additional_cost_vs_optimum_USD', ...
 'Additional_cost_vs_optimum_per_pack_USD', ...
 'Excess_cost_relative_to_optimum_percent', ...
 'Cost_reduction_from_fixed_policy_to_optimum_percent', ...
 'Cost_accounting_absolute_error_USD'});

writetable(KRegret_Curve_Table, cfg.curve_csv);

fprintf('>>> Full k-regret curve table saved: %s\n', cfg.curve_csv);

%% 5) Extract representative fixed-k benchmark scenarios
% Include the three predefined fixed-k policies and the globally optimized k.
benchmark_k_target = unique( ...
 [cfg.fixed_k_benchmarks(:); opt_k], ...
 'stable');

n_benchmark = numel(benchmark_k_target);

Scenario_label = strings(n_benchmark, 1);
Policy_definition = repmat( ...
 "Fixed k with T1 and T2 re-optimized conditional on k", ...
 n_benchmark, 1);

Benchmark_k_target = zeros(n_benchmark, 1);
Benchmark_k_actual = zeros(n_benchmark, 1);
Benchmark_k_idx = zeros(n_benchmark, 1);

Is_exact_k_match = false(n_benchmark, 1);
Is_global_optimized_k = false(n_benchmark, 1);
Thresholds_reoptimized = true(n_benchmark, 1);

Scenario_T1 = zeros(n_benchmark, 1);
Scenario_T2 = zeros(n_benchmark, 1);

Scenario_total_cost_USD = zeros(n_benchmark, 1);
Scenario_cost_per_pack_USD = zeros(n_benchmark, 1);

Scenario_O_USD = zeros(n_benchmark, 1);
Scenario_H_USD = zeros(n_benchmark, 1);
Scenario_R_USD = zeros(n_benchmark, 1);
Scenario_Model_USD = zeros(n_benchmark, 1);

Scenario_additional_cost_USD = zeros(n_benchmark, 1);
Scenario_additional_cost_per_pack_USD = zeros(n_benchmark, 1);
Scenario_excess_to_optimum_percent = zeros(n_benchmark, 1);
Scenario_cost_reduction_to_optimum_percent = zeros(n_benchmark, 1);

for ii = 1:n_benchmark

 k_target = benchmark_k_target(ii);

 idx_exact = find(k_vals == k_target, 1, 'first');

 if isempty(idx_exact)
 [~, idx_now] = min(abs(k_vals - k_target));
 is_exact = false;
 else
 idx_now = idx_exact;
 is_exact = true;
 end

 k_actual = k_vals(idx_now);
 is_global_optimum = (idx_now == opt_k_idx);

 Benchmark_k_target(ii) = k_target;
 Benchmark_k_actual(ii) = k_actual;
 Benchmark_k_idx(ii) = idx_now;

 Is_exact_k_match(ii) = is_exact;
 Is_global_optimized_k(ii) = is_global_optimum;

 if is_global_optimum
 Scenario_label(ii) = sprintf('Optimized k* = %d', k_actual);
 else
 Scenario_label(ii) = sprintf( ...
 'Fixed k = %d; thresholds re-optimized', ...
 k_actual);
 end

 Scenario_T1(ii) = T1_base(idx_now);
 Scenario_T2(ii) = T2_base(idx_now);

 Scenario_total_cost_USD(ii) = Fixed_Total_Cost(idx_now);
 Scenario_cost_per_pack_USD(ii) = Cost_per_pack_USD(idx_now);

 Scenario_O_USD(ii) = Fixed_O(idx_now);
 Scenario_H_USD(ii) = Fixed_H(idx_now);
 Scenario_R_USD(ii) = Fixed_R(idx_now);
 Scenario_Model_USD(ii) = Fixed_Model(idx_now);

 Scenario_additional_cost_USD(ii) = Additional_cost_USD(idx_now);
 Scenario_additional_cost_per_pack_USD(ii) = ...
 Additional_cost_per_pack_USD(idx_now);

 Scenario_excess_to_optimum_percent(ii) = ...
 Excess_cost_relative_to_optimum_percent(idx_now);

 Scenario_cost_reduction_to_optimum_percent(ii) = ...
 Cost_reduction_from_fixed_policy_to_optimum_percent(idx_now);
end

KRegret_Scenario_Table = table( ...
 Scenario_label, ...
 Policy_definition, ...
 Benchmark_k_target, ...
 Benchmark_k_actual, ...
 Benchmark_k_idx, ...
 Is_exact_k_match, ...
 Is_global_optimized_k, ...
 Thresholds_reoptimized, ...
 Scenario_T1, ...
 Scenario_T2, ...
 Scenario_total_cost_USD, ...
 Scenario_cost_per_pack_USD, ...
 Scenario_O_USD, ...
 Scenario_H_USD, ...
 Scenario_R_USD, ...
 Scenario_Model_USD, ...
 Scenario_additional_cost_USD, ...
 Scenario_additional_cost_per_pack_USD, ...
 Scenario_excess_to_optimum_percent, ...
 Scenario_cost_reduction_to_optimum_percent, ...
 'VariableNames', { ...
 'Scenario_label', ...
 'Policy_definition', ...
 'Target_k', ...
 'Actual_k', ...
 'k_index', ...
 'Exact_k_match', ...
 'Is_global_optimized_k', ...
 'Thresholds_reoptimized_given_k', ...
 'T1_star_given_k', ...
 'T2_star_given_k', ...
 'Total_cost_USD', ...
 'Cost_per_pack_USD', ...
 'Opportunity_loss_USD', ...
 'Mismatch_handling_rework_cost_USD', ...
 'Risk_penalty_USD', ...
 'SOH_labelling_adaptation_cost_USD', ...
 'Additional_cost_vs_optimum_USD', ...
 'Additional_cost_vs_optimum_per_pack_USD', ...
 'Excess_cost_relative_to_optimum_percent', ...
 'Cost_reduction_to_optimum_percent'});

writetable(KRegret_Scenario_Table, cfg.scenario_csv);

fprintf('>>> Representative fixed-k scenario table saved: %s\n', ...
 cfg.scenario_csv);

disp(' ');
disp('========== Fixed-scale LFP fixed-k benchmark scenarios ==========');
disp(KRegret_Scenario_Table);

%% 6) Plot the fixed-scale k-regret curve
fig_regret = figure( ...
 'Name', 'ExD06a_LFP_fixed_scale_k_regret_benchmark', ...
