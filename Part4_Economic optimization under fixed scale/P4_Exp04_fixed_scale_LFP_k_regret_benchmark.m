%% Local helper function
function [value, field_used] = local_get_first_existing_field( ...
 input_struct, candidate_fields, description)
% Return the first available field from a list of preferred/legacy names.

value = [];
field_used = '';

for ii = 1:numel(candidate_fields)

 current_field = candidate_fields{ii};

 if isfield(input_struct, current_field)
 value = input_struct.(current_field);
 field_used = current_field;
 break;
 end
end

if isempty(field_used)
 error('Required field for "%s" was not found. Checked: %s', ...
 description, strjoin(candidate_fields, ', '));
end
end
