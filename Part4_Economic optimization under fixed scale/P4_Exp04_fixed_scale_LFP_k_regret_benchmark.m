% Highlight the globally optimized policy.
h_opt = scatter( ...
 opt_k, ...
 Additional_cost_per_pack_USD(opt_k_idx), ...
 150, ...
 'p', ...
 'MarkerFaceColor', [0.85, 0.20, 0.20], ...
 'MarkerEdgeColor', 'k', ...
 'LineWidth', 1.0, ...
 'DisplayName', 'Optimized k^*');

xline( ...
 opt_k, ...
 '--', ...
 'Color', [0.85, 0.20, 0.20], ...
 'LineWidth', 1.2, ...
 'HandleVisibility', 'off');

ymax = max(Additional_cost_per_pack_USD);

