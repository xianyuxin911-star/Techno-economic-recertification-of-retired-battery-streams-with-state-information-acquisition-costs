end

ylim([0 yl]);

yyaxis right

hCond = plot(x, Ycond, '--', ...
 'Color', [0.35 0.35 0.35], ...
 'LineWidth', 1.8, ...
 'Marker', 'd', ...
 'MarkerSize', 5, ...
 'MarkerFaceColor', 'w');

ylabel('Severe-trigger rate among G1 errors');
ylim([0 1.05]);

xline(Eta_1_base, '--', 'Baseline', ...
 'LineWidth', 1.2, ...
 'LabelVerticalAlignment', 'bottom', ...
 'HandleVisibility', 'off');

grid on;
box on;

