R.T2_star = NaN;
R.A1_share = NaN;
R.A2_share = NaN;
R.A3_share = NaN;
R.Opportunity_loss_USD = NaN;
R.Verification_cost_USD = NaN;
R.Risk_penalty_USD = NaN;
R.Information_cost_USD = NaN;
R.Total_cost_USD = NaN;
R.Total_cost_per_pack_USD = NaN;
R.Decision_loss_per_pack_USD = NaN;

for kk = 1:numel(k_vals)

 k_current = k_vals(kk);

 sampled_noise = local_build_noise_by_k( ...
 k_current, ...
 k_emp, k_anchor, k_bridge_end, ...
 err_cell, ...
 w_curve, muM_curve, sigM_curve, muR_curve, sigR_curve, rmse_target, k_vals, ...
 base_u, base_z, base_pick, base_bridge);

 s_pred = min(max(s_true + sampled_noise, 0), 1.0);

 best_decision_loss_k = inf;

 best_T1_k = NaN;
 best_T2_k = NaN;
 best_O_k = NaN;
 best_A_k = NaN;
 best_R_k = NaN;
 best_Pred_Action_k = [];

 for i_t1 = 1:numel(T1_grid)

 T1 = T1_grid(i_t1);

 for i_t2 = 1:numel(T2_grid)

 T2 = T2_grid(i_t2);

 if T1 >= T2
 continue;
 end

 Pred_Action = 3 * ones(N_sim, 1);
 Pred_Action(s_pred >= T1 & s_pred < T2) = 2;
 Pred_Action(s_pred >= T2) = 1;

 idx_pred = sub2ind([N_sim, 3], (1:N_sim)', Pred_Action);
 O_arr = Pi_True_Max - Pi_True(idx_pred);

 is_G1_error = ...
 (True_Action_i == 3 & Pred_Action == 2) | ...
 (True_Action_i == 2 & Pred_Action == 3);

 is_G2_error = ...
 (True_Action_i == 1 & Pred_Action == 2) | ...
 (True_Action_i == 2 & Pred_Action == 1);

 Current_Eta = zeros(N_sim, 1);
 Current_Eta(is_G1_error) = eta1_now;
 Current_Eta(is_G2_error) = eta2_now;

 trigger_penalty = O_arr > Current_Eta;
 Excess_Loss = max(0, O_arr - Current_Eta);

 A_arr = trigger_penalty .* Lambda_A;
 R_arr = (Pred_Action < True_Action_i) .* ...
 trigger_penalty .* (Lambda_R .* Excess_Loss);

 decision_loss = mean(O_arr + A_arr + R_arr);

 if decision_loss < best_decision_loss_k

 best_decision_loss_k = decision_loss;
 best_T1_k = T1;
 best_T2_k = T2;

 best_O_k = mean(O_arr);
 best_A_k = mean(A_arr);
 best_R_k = mean(R_arr);
 best_Pred_Action_k = Pred_Action;
 end
 end
 end

 information_cost_k = k_current * c_pack + c_train;

 total_cost_k = ...
 Fixed_Scale_N * best_decision_loss_k + ...
 information_cost_k;

 if total_cost_k < best_total_cost

 best_total_cost = total_cost_k;

 R.k_star = k_current;
 R.T1_star = best_T1_k;
 R.T2_star = best_T2_k;

 R.A1_share = mean(best_Pred_Action_k == 1);
 R.A2_share = mean(best_Pred_Action_k == 2);
 R.A3_share = mean(best_Pred_Action_k == 3);

 R.Opportunity_loss_USD = Fixed_Scale_N * best_O_k;
 R.Verification_cost_USD = Fixed_Scale_N * best_A_k;
 R.Risk_penalty_USD = Fixed_Scale_N * best_R_k;
 R.Information_cost_USD = information_cost_k;

 R.Total_cost_USD = best_total_cost;
 R.Total_cost_per_pack_USD = best_total_cost / Fixed_Scale_N;
 R.Decision_loss_per_pack_USD = best_decision_loss_k;
 end
end
end

