%% Local helper functions
function M = local_evaluate_eta_policy( ...
 eta1_now, ...
 eta2_now, ...
 O_arr, ...
 risk_indicator, ...
 is_G1_error, ...
 is_G2_error, ...
 is_other_error, ...
 is_A3_to_A2, ...
 is_A2_to_A3, ...
 is_A1_to_A2, ...
 is_A2_to_A1, ...
 Lambda_A, ...
 Lambda_R, ...
 Fixed_Scale_N, ...
 Information_cost)
% Evaluate trigger rates and cost terms for a fixed decision population.

N_sim = numel(O_arr);

Current_Eta = zeros(N_sim, 1);
Current_Eta(is_G1_error) = eta1_now;
Current_Eta(is_G2_error) = eta2_now;

trigger_penalty = O_arr > Current_Eta;
Excess_Loss = max(0, O_arr - Current_Eta);

A_arr = trigger_penalty .* Lambda_A;
R_arr = risk_indicator .* trigger_penalty .* (Lambda_R .* Excess_Loss);

penalty_cost_arr = A_arr + R_arr;

M = struct();

% Error rates.
M.G1_error_rate_total = mean(is_G1_error);
M.G2_error_rate_total = mean(is_G2_error);
M.Other_error_rate_total = mean(is_other_error);

% Trigger rates.
M.G1_trigger_rate_total = mean(is_G1_error & trigger_penalty);
M.G2_trigger_rate_total = mean(is_G2_error & trigger_penalty);
M.Other_trigger_rate_total = mean(is_other_error & trigger_penalty);

M.G1_trigger_rate_conditional = local_conditional_rate(is_G1_error, trigger_penalty);
M.G2_trigger_rate_conditional = local_conditional_rate(is_G2_error, trigger_penalty);

M.A3toA2_trigger_rate_total = mean(is_A3_to_A2 & trigger_penalty);
M.A2toA3_trigger_rate_total = mean(is_A2_to_A3 & trigger_penalty);
M.A1toA2_trigger_rate_total = mean(is_A1_to_A2 & trigger_penalty);
M.A2toA1_trigger_rate_total = mean(is_A2_to_A1 & trigger_penalty);

M.A3toA2_trigger_rate_conditional = local_conditional_rate(is_A3_to_A2, trigger_penalty);
M.A2toA3_trigger_rate_conditional = local_conditional_rate(is_A2_to_A3, trigger_penalty);
M.A1toA2_trigger_rate_conditional = local_conditional_rate(is_A1_to_A2, trigger_penalty);
M.A2toA1_trigger_rate_conditional = local_conditional_rate(is_A2_to_A1, trigger_penalty);

M.Overall_trigger_rate = mean(trigger_penalty);
M.Risk_trigger_rate = mean(risk_indicator & trigger_penalty);

% Cost terms.
M.Opportunity_loss_USD = Fixed_Scale_N * mean(O_arr);
M.Verification_cost_USD = Fixed_Scale_N * mean(A_arr);
M.Risk_penalty_USD = Fixed_Scale_N * mean(R_arr);

M.A3toA2_penalty_cost_USD = Fixed_Scale_N * mean(penalty_cost_arr .* is_A3_to_A2);
M.A2toA3_penalty_cost_USD = Fixed_Scale_N * mean(penalty_cost_arr .* is_A2_to_A3);
M.A1toA2_penalty_cost_USD = Fixed_Scale_N * mean(penalty_cost_arr .* is_A1_to_A2);
M.A2toA1_penalty_cost_USD = Fixed_Scale_N * mean(penalty_cost_arr .* is_A2_to_A1);
M.Other_penalty_cost_USD = Fixed_Scale_N * mean(penalty_cost_arr .* is_other_error);

M.Total_decision_loss_USD = ...
 M.Opportunity_loss_USD + ...
 M.Verification_cost_USD + ...
 M.Risk_penalty_USD;

M.Information_cost_USD = Information_cost;

M.Total_cost_USD = ...
 M.Total_decision_loss_USD + ...
 M.Information_cost_USD;

M.Total_cost_per_pack_USD = M.Total_cost_USD / Fixed_Scale_N;
end

function r = local_conditional_rate(mask, trigger)
% Compute the trigger rate conditional on a specified error mask.

den = sum(mask);

if den > 0
 r = sum(mask & trigger) / den;
else
 r = NaN;
end
end

