%% P4_Exp10_LFP_GrayZoneToleranceMechanism.m
% Purpose:
% This script analyzes the gray-zone tolerance mechanism under the fixed-scale LFP
% baseline policy and generates Supplementary Fig. 19.
%
% The baseline policy, including k*, T1*, and T2*, is first fixed to analyze severe-trigger
% rates and penalty-related costs under eta_1 and eta_2 sensitivity. The script then
% re-optimizes k, T1, and T2 under each eta setting to compare fixed-policy and re-optimized-policy costs.
% The re-optimization uses the shared Part4 residual generator.


clear; clc; close all;

set(groot, 'DefaultFigureVisible', 'on');

%% 1) Configuration and path setup
cfg = struct();

script_dir = fileparts(mfilename('fullpath'));

if isempty(script_dir)
 script_dir = pwd;
end

cfg.part4_dir = script_dir;

cfg.output_dir = fullfile(cfg.part4_dir, 'Output');
cfg.results_dir = fullfile(cfg.output_dir, 'Results');

cfg.figure_root = fullfile(cfg.part4_dir, 'Figures');
cfg.supp_fig_dir = fullfile(cfg.figure_root, 'Supplementary');

cfg.input_mat = fullfile(cfg.results_dir, 'P4_Exp01_FixedScale_LFP_Workspace.mat');

cfg.function_dir = fullfile(cfg.part4_dir, 'Function');

% Output tables.
cfg.eta1_fixed_csv = fullfile(cfg.results_dir, 'P4_Exp10_LFP_GrayZoneToleranceMechanism_FixedPolicy_Eta1.csv');
cfg.eta2_fixed_csv = fullfile(cfg.results_dir, 'P4_Exp10_LFP_GrayZoneToleranceMechanism_FixedPolicy_Eta2.csv');

cfg.eta1_reopt_csv = fullfile(cfg.results_dir, 'P4_Exp10_LFP_GrayZoneToleranceMechanism_Reoptimized_Eta1.csv');
cfg.eta2_reopt_csv = fullfile(cfg.results_dir, 'P4_Exp10_LFP_GrayZoneToleranceMechanism_Reoptimized_Eta2.csv');

cfg.eta1_compare_csv = fullfile(cfg.results_dir, 'P4_Exp10_LFP_GrayZoneToleranceMechanism_FixedVsReoptimized_Eta1.csv');
cfg.eta2_compare_csv = fullfile(cfg.results_dir, 'P4_Exp10_LFP_GrayZoneToleranceMechanism_FixedVsReoptimized_Eta2.csv');

cfg.result_mat = fullfile(cfg.results_dir, 'P4_Exp10_LFP_GrayZoneToleranceMechanism_Results.mat');

% Output figures.
cfg.fig_eta1_trigger = fullfile(cfg.supp_fig_dir, 'SupFig19a_LFP_gray_zone_Eta1_trigger_rate_composition.fig');
cfg.png_eta1_trigger = fullfile(cfg.supp_fig_dir, 'SupFig19a_LFP_gray_zone_Eta1_trigger_rate_composition.png');

cfg.fig_eta1_cost = fullfile(cfg.supp_fig_dir, 'SupFig19b_LFP_gray_zone_Eta1_penalty_cost_composition.fig');
cfg.png_eta1_cost = fullfile(cfg.supp_fig_dir, 'SupFig19b_LFP_gray_zone_Eta1_penalty_cost_composition.png');

cfg.fig_eta2_trigger = fullfile(cfg.supp_fig_dir, 'SupFig19c_LFP_gray_zone_Eta2_trigger_rate_composition.fig');
cfg.png_eta2_trigger = fullfile(cfg.supp_fig_dir, 'SupFig19c_LFP_gray_zone_Eta2_trigger_rate_composition.png');

cfg.fig_eta2_cost = fullfile(cfg.supp_fig_dir, 'SupFig19d_LFP_gray_zone_Eta2_penalty_cost_composition.fig');
cfg.png_eta2_cost = fullfile(cfg.supp_fig_dir, 'SupFig19d_LFP_gray_zone_Eta2_penalty_cost_composition.png');

cfg.fig_eta1_compare = fullfile(cfg.supp_fig_dir, 'SupFig19e_LFP_gray_zone_fixed_vs_reoptimized_Eta1.fig');
cfg.png_eta1_compare = fullfile(cfg.supp_fig_dir, 'SupFig19e_LFP_gray_zone_fixed_vs_reoptimized_Eta1.png');

cfg.fig_eta2_compare = fullfile(cfg.supp_fig_dir, 'SupFig19f_LFP_gray_zone_fixed_vs_reoptimized_Eta2.fig');
cfg.png_eta2_compare = fullfile(cfg.supp_fig_dir, 'SupFig19f_LFP_gray_zone_fixed_vs_reoptimized_Eta2.png');

cfg.png_resolution = 600;

% Re-optimization control.
cfg.do_reoptimize = true;

folder_list = { ...
 cfg.output_dir, ...
 cfg.results_dir, ...
 cfg.figure_root, ...
 cfg.supp_fig_dir};

for ii = 1:numel(folder_list)
 if ~exist(folder_list{ii}, 'dir')
 mkdir(folder_list{ii});
 end
end

addpath(cfg.function_dir);

if exist('local_build_noise_by_k', 'file') ~= 2
 error('Function local_build_noise_by_k.m was not found in: %s', cfg.function_dir);
end

fprintf('================ P4 Exp10: LFP gray-zone tolerance mechanism ================\n');
fprintf('Input workspace file: %s\n', cfg.input_mat);
fprintf('Function folder: %s\n', cfg.function_dir);
fprintf('Output folder: %s\n', cfg.results_dir);
fprintf('Figure folder: %s\n', cfg.supp_fig_dir);

%% 2) Load the updated fixed-scale LFP workspace
if ~isfile(cfg.input_mat)
 error(['Input file not found: %s\n' ...
 'Please run P4_Exp01_FixedScale_LFP_Optimization.m first to generate the fixed-scale LFP workspace.'], ...
 cfg.input_mat);
end

S = load(cfg.input_mat);

required_vars = { ...
 's_true', ...
 'Pi_True', ...
 'Pi_True_Max', ...
 'True_Action_i', ...
 'Eta_1', ...
 'Eta_2', ...
 'Lambda_A', ...
 'Lambda_R', ...
 'Fixed_Scale_N', ...
 'c_pack', ...
 'c_train', ...
 'k_vals', ...
 'k_emp', ...
 'k_anchor', ...
 'k_bridge_end', ...
 'err_cell', ...
 'w_curve', ...
 'muM_curve', ...
 'sigM_curve', ...
 'muR_curve', ...
 'sigR_curve', ...
 'rmse_target', ...
 'base_u', ...
 'base_z', ...
 'base_pick', ...
 'base_bridge'};

for ii = 1:numel(required_vars)
 if ~isfield(S, required_vars{ii})
 error('Required variable is missing from %s: %s', ...
 cfg.input_mat, required_vars{ii});
 end
end

% Basic sample-level variables.
s_true = S.s_true(:);
Pi_True = S.Pi_True;
Pi_True_Max = S.Pi_True_Max(:);
True_Action_i = S.True_Action_i(:);

N_sim = numel(s_true);

if size(Pi_True, 1) ~= N_sim || size(Pi_True, 2) ~= 3
 error('Pi_True must have size [N_sim, 3].');
end

if numel(Pi_True_Max) ~= N_sim || numel(True_Action_i) ~= N_sim
 error('Pi_True_Max and True_Action_i must have length N_sim.');
end

% Baseline gray-zone and penalty parameters.
Eta_1_base = S.Eta_1;
Eta_2_base = S.Eta_2;

Lambda_A = S.Lambda_A;
Lambda_R = S.Lambda_R;

Fixed_Scale_N = S.Fixed_Scale_N;
c_pack = S.c_pack;
c_train = S.c_train;

% Residual model and common random-number variables.
k_vals = S.k_vals(:);
k_emp = S.k_emp(:);
k_anchor = S.k_anchor;
k_bridge_end = S.k_bridge_end;
err_cell = S.err_cell;

w_curve = S.w_curve(:);
muM_curve = S.muM_curve(:);
sigM_curve = S.sigM_curve(:);
muR_curve = S.muR_curve(:);
sigR_curve = S.sigR_curve(:);
rmse_target = S.rmse_target(:);

base_u = S.base_u(:);
base_z = S.base_z(:);
base_pick = S.base_pick(:);
base_bridge = S.base_bridge(:);

if numel(base_u) ~= N_sim || numel(base_z) ~= N_sim || ...
 numel(base_pick) ~= N_sim || numel(base_bridge) ~= N_sim
 error('base_u, base_z, base_pick, and base_bridge must have length N_sim.');
end

% Threshold grids.
if isfield(S, 'T1_grid')
 T1_grid = S.T1_grid;
else
 T1_grid = 0.400:0.005:0.500;
 warning('T1_grid was not found. Default LFP T1_grid = 0.400:0.005:0.500 was used.');
end

if isfield(S, 'T2_grid')
 T2_grid = S.T2_grid;
else
 T2_grid = 0.850:0.005:1.000;
 warning('T2_grid was not found. Default LFP T2_grid = 0.850:0.005:1.000 was used.');
end

% Baseline policy.
if isfield(S, 'opt_k') && isfield(S, 'opt_T1') && isfield(S, 'opt_T2')

 opt_k = S.opt_k;
 opt_T1 = S.opt_T1;
 opt_T2 = S.opt_T2;

elseif isfield(S, 'Fixed_Total_Cost') && ...
 isfield(S, 'T1_base') && ...
 isfield(S, 'T2_base')

 [~, idx0] = min(S.Fixed_Total_Cost);

 opt_k = k_vals(idx0);
 opt_T1 = S.T1_base(idx0);
 opt_T2 = S.T2_base(idx0);

else

 error(['Baseline policy not found. Required variables are opt_k/opt_T1/opt_T2, ' ...
 'or Fixed_Total_Cost/T1_base/T2_base.']);
end

Information_cost_fixed = opt_k * c_pack + c_train;

fprintf('Updated fixed-scale LFP workspace loaded.\n');
fprintf('Baseline policy: k* = %d, T1* = %.4f, T2* = %.4f\n', ...
 opt_k, opt_T1, opt_T2);
fprintf('Baseline Eta_1 = %.4f, Eta_2 = %.4f\n', Eta_1_base, Eta_2_base);
fprintf('Lambda_A = %.4f, Lambda_R = %.4f\n', Lambda_A, Lambda_R);
fprintf('Fixed deployment scale N = %d packs\n', Fixed_Scale_N);
fprintf('Re-optimization grid: %d k values, %d T1 values, %d T2 values\n', ...
 numel(k_vals), numel(T1_grid), numel(T2_grid));

%% 3) Use the saved optimal-policy decision population from the updated TEA workspace

% Predicted SOH under the optimized baseline policy.
if isfield(S, 's_pred_opt')
 s_pred_fixed = S.s_pred_opt(:);
else
 s_pred_fixed = nan(N_sim, 1);
 warning('s_pred_opt was not found. Sup19 will continue without predicted-SOH diagnostics.');
end

if numel(s_pred_fixed) ~= N_sim
 error('s_pred_opt must have length N_sim.');
end

% Predicted action under the optimized baseline policy.
if isfield(S, 'Pred_Action_opt')

 Pred_Action_fixed = S.Pred_Action_opt(:);

elseif isfield(S, 's_pred_opt')

 Pred_Action_fixed = 3 * ones(N_sim, 1);
 Pred_Action_fixed(s_pred_fixed >= opt_T1 & s_pred_fixed < opt_T2) = 2;
 Pred_Action_fixed(s_pred_fixed >= opt_T2) = 1;

 warning('Pred_Action_opt was not found. It was reconstructed from s_pred_opt and the optimized thresholds.');

else

 error('Neither Pred_Action_opt nor s_pred_opt was found in the workspace.');
end

if numel(Pred_Action_fixed) ~= N_sim
 error('Pred_Action_opt must have length N_sim.');
end

% Opportunity loss under the optimized baseline policy.
if isfield(S, 'O_arr_opt')

 O_arr_fixed = S.O_arr_opt(:);

else

 idx_pred_fixed = sub2ind([N_sim, 3], (1:N_sim)', Pred_Action_fixed);
 O_arr_fixed = Pi_True_Max - Pi_True(idx_pred_fixed);

 warning('O_arr_opt was not found. It was reconstructed from Pi_True and Pred_Action_opt.');
end

if numel(O_arr_fixed) ~= N_sim
 error('O_arr_opt must have length N_sim.');
end

idx_pred_fixed = sub2ind([N_sim, 3], (1:N_sim)', Pred_Action_fixed);
Chosen_Value_fixed = Pi_True(idx_pred_fixed);

% G1: A3 <-> A2.
is_G1_error_fixed = ...
 (True_Action_i == 3 & Pred_Action_fixed == 2) | ...
 (True_Action_i == 2 & Pred_Action_fixed == 3);

% G2: A1 <-> A2.
is_G2_error_fixed = ...
 (True_Action_i == 1 & Pred_Action_fixed == 2) | ...
 (True_Action_i == 2 & Pred_Action_fixed == 1);

% Non-adjacent action mismatch.
is_any_error_fixed = Pred_Action_fixed ~= True_Action_i;
is_other_error_fixed = is_any_error_fixed & ...
 ~is_G1_error_fixed & ...
 ~is_G2_error_fixed;

% Directional decomposition within G1.
is_A3_to_A2_fixed = (True_Action_i == 3 & Pred_Action_fixed == 2);
is_A2_to_A3_fixed = (True_Action_i == 2 & Pred_Action_fixed == 3);

% Directional decomposition within G2.
is_A1_to_A2_fixed = (True_Action_i == 1 & Pred_Action_fixed == 2);
is_A2_to_A1_fixed = (True_Action_i == 2 & Pred_Action_fixed == 1);

% Over-optimistic assignment.
risk_indicator_fixed = Pred_Action_fixed < True_Action_i;

% Baseline action allocation.
A1_share_fixed = mean(Pred_Action_fixed == 1);
A2_share_fixed = mean(Pred_Action_fixed == 2);
A3_share_fixed = mean(Pred_Action_fixed == 3);

True_A1_share = mean(True_Action_i == 1);
True_A2_share = mean(True_Action_i == 2);
True_A3_share = mean(True_Action_i == 3);

fprintf('Predicted action shares: A1 = %.4f, A2 = %.4f, A3 = %.4f\n', ...
 A1_share_fixed, A2_share_fixed, A3_share_fixed);

fprintf('True optimal action shares: A1 = %.4f, A2 = %.4f, A3 = %.4f\n', ...
 True_A1_share, True_A2_share, True_A3_share);

fprintf('Mismatch rates: G1 = %.4f, G2 = %.4f, other = %.4f\n', ...
 mean(is_G1_error_fixed), mean(is_G2_error_fixed), mean(is_other_error_fixed));

%% 4) Define eta scanning ranges
Eta_1_list = [0 5 10 20 30 40 50 60 80 100 120]';
Eta_2_list = [0 5 10 20 30 40 50 60 80 100 120]';

%% 5) Scan Eta_1 under the fixed baseline policy
Result_Eta1_fixed = table();

for ii = 1:numel(Eta_1_list)

 eta1_now = Eta_1_list(ii);
 eta2_now = Eta_2_base;

 M = local_evaluate_eta_policy( ...
 eta1_now, ...
 eta2_now, ...
 O_arr_fixed, ...
 risk_indicator_fixed, ...
 is_G1_error_fixed, ...
 is_G2_error_fixed, ...
 is_other_error_fixed, ...
 is_A3_to_A2_fixed, ...
 is_A2_to_A3_fixed, ...
 is_A1_to_A2_fixed, ...
 is_A2_to_A1_fixed, ...
 Lambda_A, ...
 Lambda_R, ...
 Fixed_Scale_N, ...
 Information_cost_fixed);

 curr_row = local_build_fixed_row( ...
 eta1_now, eta2_now, opt_k, opt_T1, opt_T2, ...
 A1_share_fixed, A2_share_fixed, A3_share_fixed, M, ...
 "Eta1");

 Result_Eta1_fixed = [Result_Eta1_fixed; curr_row]; %#ok<AGROW>
end

%% 6) Scan Eta_2 under the fixed baseline policy
Result_Eta2_fixed = table();

for ii = 1:numel(Eta_2_list)

 eta1_now = Eta_1_base;
 eta2_now = Eta_2_list(ii);

 M = local_evaluate_eta_policy( ...
 eta1_now, ...
 eta2_now, ...
 O_arr_fixed, ...
 risk_indicator_fixed, ...
 is_G1_error_fixed, ...
 is_G2_error_fixed, ...
 is_other_error_fixed, ...
 is_A3_to_A2_fixed, ...
 is_A2_to_A3_fixed, ...
 is_A1_to_A2_fixed, ...
 is_A2_to_A1_fixed, ...
 Lambda_A, ...
 Lambda_R, ...
 Fixed_Scale_N, ...
 Information_cost_fixed);

 curr_row = local_build_fixed_row( ...
 eta1_now, eta2_now, opt_k, opt_T1, opt_T2, ...
 A1_share_fixed, A2_share_fixed, A3_share_fixed, M, ...
 "Eta2");

 Result_Eta2_fixed = [Result_Eta2_fixed; curr_row]; %#ok<AGROW>
end

%% 7) Re-optimize k, T1, and T2 under each eta setting
Result_Eta1_reopt = table();
Result_Eta2_reopt = table();

if cfg.do_reoptimize

 fprintf('\n================ Re-optimization under eta sensitivity ================\n');

 for ii = 1:numel(Eta_1_list)

 eta1_now = Eta_1_list(ii);
 eta2_now = Eta_2_base;

 fprintf('[Eta1 reopt] %d / %d | eta_1 = %.2f, eta_2 = %.2f\n', ...
 ii, numel(Eta_1_list), eta1_now, eta2_now);

 R = local_reoptimize_policy( ...
 eta1_now, eta2_now, ...
 s_true, Pi_True, Pi_True_Max, True_Action_i, ...
