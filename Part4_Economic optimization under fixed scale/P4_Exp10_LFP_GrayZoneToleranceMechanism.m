 'Reoptimized_k_star', ...
 'Reoptimized_T1_star', ...
 'Reoptimized_T2_star'});

 Compare_Eta2 = table( ...
 Result_Eta2_fixed.Eta_2, ...
 Result_Eta2_fixed.Total_cost_per_pack_USD, ...
 Result_Eta2_reopt.Total_cost_per_pack_USD, ...
 Result_Eta2_fixed.Fixed_k, ...
 Result_Eta2_reopt.k_star, ...
 Result_Eta2_reopt.T1_star, ...
 Result_Eta2_reopt.T2_star, ...
 'VariableNames', { ...
