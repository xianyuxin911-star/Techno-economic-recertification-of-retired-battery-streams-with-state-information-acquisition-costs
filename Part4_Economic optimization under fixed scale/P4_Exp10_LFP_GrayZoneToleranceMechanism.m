end

%% 9) Save result tables
writetable(Result_Eta1_fixed, cfg.eta1_fixed_csv);
writetable(Result_Eta2_fixed, cfg.eta2_fixed_csv);

fprintf('Eta1 fixed-policy table saved: %s\n', cfg.eta1_fixed_csv);
