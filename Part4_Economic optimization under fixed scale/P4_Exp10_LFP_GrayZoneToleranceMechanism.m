 R.Risk_penalty_USD, ...
 R.Information_cost_USD, ...
 R.Total_cost_USD, ...
 R.Total_cost_per_pack_USD, ...
 R.Decision_loss_per_pack_USD, ...
 'VariableNames', { ...
 'Eta_1', ...
 'Eta_2_fixed', ...
 'k_star', ...
 'T1_star', ...
 'T2_star', ...
 'A1_share', ...
 'A2_share', ...
 'A3_share', ...
 'Opportunity_loss_USD', ...
 'Verification_cost_USD', ...
 'Risk_penalty_USD', ...
 'Information_cost_USD', ...
 'Total_cost_USD', ...
 'Total_cost_per_pack_USD', ...
 'Decision_loss_per_pack_USD'});

 Result_Eta1_reopt = [Result_Eta1_reopt; curr_row]; %#ok<AGROW>
 end

 for ii = 1:numel(Eta_2_list)

 eta1_now = Eta_1_base;
 eta2_now = Eta_2_list(ii);

 fprintf('[Eta2 reopt] %d / %d | eta_1 = %.2f, eta_2 = %.2f\n', ...
 ii, numel(Eta_2_list), eta1_now, eta2_now);

 R = local_reoptimize_policy( ...
 eta1_now, eta2_now, ...
 s_true, Pi_True, Pi_True_Max, True_Action_i, ...
 k_vals, T1_grid, T2_grid, ...
 k_emp, k_anchor, k_bridge_end, ...
 err_cell, ...
 w_curve, muM_curve, sigM_curve, muR_curve, sigR_curve, rmse_target, ...
 base_u, base_z, base_pick, base_bridge, ...
 Lambda_A, Lambda_R, ...
 Fixed_Scale_N, c_pack, c_train);

 curr_row = table( ...
 eta1_now, ...
 eta2_now, ...
 R.k_star, ...
 R.T1_star, ...
 R.T2_star, ...
 R.A1_share, ...
 R.A2_share, ...
 R.A3_share, ...
 R.Opportunity_loss_USD, ...
 R.Verification_cost_USD, ...
 R.Risk_penalty_USD, ...
 R.Information_cost_USD, ...
 R.Total_cost_USD, ...
 R.Total_cost_per_pack_USD, ...
 R.Decision_loss_per_pack_USD, ...
 'VariableNames', { ...
 'Eta_1_fixed', ...
 'Eta_2', ...
 'k_star', ...
 'T1_star', ...
 'T2_star', ...
 'A1_share', ...
 'A2_share', ...
 'A3_share', ...
 'Opportunity_loss_USD', ...
 'Verification_cost_USD', ...
 'Risk_penalty_USD', ...
 'Information_cost_USD', ...
 'Total_cost_USD', ...
 'Total_cost_per_pack_USD', ...
 'Decision_loss_per_pack_USD'});

 Result_Eta2_reopt = [Result_Eta2_reopt; curr_row]; %#ok<AGROW>
 end
end

%% 8) Build fixed-policy versus re-optimized comparison tables
if cfg.do_reoptimize

 Compare_Eta1 = table( ...
 Result_Eta1_fixed.Eta_1, ...
 Result_Eta1_fixed.Total_cost_per_pack_USD, ...
