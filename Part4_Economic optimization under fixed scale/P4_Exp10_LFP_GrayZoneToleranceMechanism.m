 writetable(Compare_Eta2, cfg.eta2_compare_csv);

 fprintf('Eta1 re-optimized table saved: %s\n', cfg.eta1_reopt_csv);
 fprintf('Eta2 re-optimized table saved: %s\n', cfg.eta2_reopt_csv);
 fprintf('Eta1 comparison table saved: %s\n', cfg.eta1_compare_csv);
 fprintf('Eta2 comparison table saved: %s\n', cfg.eta2_compare_csv);
end

%% 10) Generate and save figures
eta_xlim = [0 120];
