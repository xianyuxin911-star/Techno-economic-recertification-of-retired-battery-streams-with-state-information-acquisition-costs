function [conditional_mean, effective_sample_size] = ...
 local_kernel_conditional_mean( ...
 x, y, x_grid, bandwidth, minimum_effective_sample_size)
% Estimate E[y | x] using a Gaussian kernel and a common bandwidth.

x = double(x(:));
y = double(y);

valid_rows = isfinite(x) & all(isfinite(y), 2);

x = x(valid_rows);
y = y(valid_rows, :);

if isempty(x)

 error('No finite sample rows were available for kernel smoothing.');
end

n_grid = numel(x_grid);
n_actions = size(y, 2);

conditional_mean = nan(n_grid, n_actions);
effective_sample_size = zeros(n_grid, 1);

for ii = 1:n_grid

 weights = exp( ...
 -0.5 .* ((x - x_grid(ii)) ./ bandwidth).^2);

 weight_sum = sum(weights);
 weight_square_sum = sum(weights.^2);

 if weight_sum <= 1e-12 || weight_square_sum <= 1e-12
 continue;
 end

 effective_sample_size(ii) = ...
 weight_sum.^2 ./ weight_square_sum;

 if effective_sample_size(ii) < minimum_effective_sample_size
 continue;
 end

 conditional_mean(ii, :) = ...
 sum(weights .* y, 1) ./ weight_sum;
end
end

function crossings = local_find_zero_crossings(x, y)
% Find all linear-interpolated zero crossings in a one-dimensional curve.

x = double(x(:));
y = double(y(:));

crossings = [];

for ii = 1:(numel(x) - 1)

 x1 = x(ii);
 x2 = x(ii + 1);

 y1 = y(ii);
 y2 = y(ii + 1);

 if ~isfinite(x1) || ~isfinite(x2) || ...
 ~isfinite(y1) || ~isfinite(y2)
 continue;
 end

 if y1 == 0

 crossings(end + 1, 1) = x1; %#ok<AGROW>

 elseif y1 * y2 < 0

 crossing_now = x1 - y1 .* (x2 - x1) ./ (y2 - y1);
 crossings(end + 1, 1) = crossing_now; %#ok<AGROW>
 end
end

if ~isempty(y) && isfinite(y(end)) && y(end) == 0
 crossings(end + 1, 1) = x(end); %#ok<AGROW>
end

if ~isempty(crossings)

 crossings = unique(round(crossings, 6), 'stable');
end
end

