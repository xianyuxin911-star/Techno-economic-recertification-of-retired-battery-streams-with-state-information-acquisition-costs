%% =========================================================================
% P4_Exp03_chemistry_comparison_summary_plots.m
%
% Purpose:
% This script generates the fixed-scale LFP-versus-NMC chemistry-comparison
% outputs used in Main Fig. 3e-f.
%
% The comparison is constructed from the Part4 Exp01 and Exp02
% sample-level outputs:
% 1) chemistry-specific expected net-value curves are re-estimated using
% the same kernel smoother and a common predicted-SOH grid;
% 2) the A3-A2 and A1-A2 expected net-value gaps are compared;
% 3) chemistry-specific optimized actions are compared on the same
% simulated true-SOH population.
%
% Important interpretation:
% - The zero crossings in the expected net-value curves are conditional
% predicted-SOH crossings. They are not true-SOH benchmark thresholds.
% - The action-transition plot uses each chemistry's own predicted SOH,
% proxy-error model and optimized thresholds. The common unit of
% comparison is the same simulated true-SOH population.
%% =========================================================================

clear;
clc;
close all;

%% 1) Configuration and path setup
cfg = struct();

script_dir = fileparts(mfilename('fullpath'));

if isempty(script_dir)
 script_dir = pwd;
end

% Part4 folders.
cfg.output_dir = fullfile(script_dir, 'Output');
cfg.results_dir = fullfile(cfg.output_dir, 'Results');

cfg.figure_root = fullfile(script_dir, 'Figures');
cfg.main_fig_dir = fullfile(cfg.figure_root, 'Main');

% Primary input files generated by Part4 Exp01 and Exp02.
cfg.lfp_result_mat_primary = fullfile(cfg.results_dir, ...
 'P4_Exp01_FixedScale_LFP_Results.mat');

cfg.nmc_result_mat_primary = fullfile(cfg.results_dir, ...
 'P4_Exp02_FixedScale_NMC_Results.mat');

% Legacy fallback files.
cfg.lfp_result_mat_legacy = fullfile(cfg.output_dir, ...
 'Result01_FixedScale_LFP_Results.mat');

cfg.nmc_result_mat_legacy = fullfile(cfg.output_dir, ...
 'Result02_FixedScale_NMC_Results.mat');

cfg.lfp_result_mat = local_select_existing_file( ...
 cfg.lfp_result_mat_primary, ...
 cfg.lfp_result_mat_legacy, ...
 'LFP fixed-scale result');

cfg.nmc_result_mat = local_select_existing_file( ...
 cfg.nmc_result_mat_primary, ...
 cfg.nmc_result_mat_legacy, ...
 'NMC fixed-scale result');

% Output files.
cfg.boundary_gap_csv = fullfile(cfg.results_dir, ...
 'P4_Exp03_ChemistryComparison_BoundaryGapZeroCrossings.csv');

cfg.representative_soh_csv = fullfile(cfg.results_dir, ...
 'P4_Exp03_ChemistryComparison_RepresentativeSOH.csv');

cfg.transition_csv = fullfile(cfg.results_dir, ...
 'P4_Exp03_ChemistryComparison_ActionTransitionMatrix.csv');

cfg.action_share_csv = fullfile(cfg.results_dir, ...
 'P4_Exp03_ChemistryComparison_ActionShares.csv');

cfg.curve_csv = fullfile(cfg.results_dir, ...
 'P4_Exp03_ChemistryComparison_CommonGridCurves.csv');

cfg.result_mat = fullfile(cfg.results_dir, ...
 'P4_Exp03_ChemistryComparison_Results.mat');

% Main Fig. 3 outputs.
cfg.fig_value_gap = fullfile(cfg.main_fig_dir, ...
 'MainFig03f_refurbishment_recycling_value_gap.fig');

cfg.png_value_gap = fullfile(cfg.main_fig_dir, ...
 'MainFig03f_refurbishment_recycling_value_gap.png');

cfg.fig_alluvial = fullfile(cfg.main_fig_dir, ...
 'MainFig03e_LFP_NMC_action_share_reallocation.fig');

cfg.png_alluvial = fullfile(cfg.main_fig_dir, ...
 'MainFig03e_LFP_NMC_action_share_reallocation.png');

% Common curve-estimation settings.
cfg.soh_grid_min = 0.40;
cfg.soh_grid_max = 1.00;
cfg.soh_grid_step = 0.001;
cfg.kernel_bandwidth = 0.008;
cfg.minimum_effective_sample_size = 30;

% Common-population tolerance.
cfg.population_soh_tolerance = 1e-12;

% Export settings.
cfg.png_resolution = 600;

folder_list = { ...
 cfg.output_dir, ...
 cfg.results_dir, ...
 cfg.figure_root, ...
 cfg.main_fig_dir};

for ii = 1:numel(folder_list)

 if ~exist(folder_list{ii}, 'dir')

 [status, message] = mkdir(folder_list{ii});

 if ~status
 error('Unable to create folder:\n%s\nReason: %s', ...
 folder_list{ii}, message);
 end
 end
end

fprintf('================ Part4 Exp03: chemistry-comparison summary ================\n');
fprintf('LFP result file: %s\n', cfg.lfp_result_mat);
fprintf('NMC result file: %s\n', cfg.nmc_result_mat);
fprintf('Common kernel bandwidth: %.4f SOH\n', cfg.kernel_bandwidth);
fprintf('Main figure folder: %s\n', cfg.main_fig_dir);

%% 2) Load and validate fixed-scale optimization results
S_LFP = load(cfg.lfp_result_mat);
S_NMC = load(cfg.nmc_result_mat);

if ~isfield(S_LFP, 'LFP_fixed_result')
 error('LFP_fixed_result was not found in:\n%s', cfg.lfp_result_mat);
end

if ~isfield(S_NMC, 'NMC_fixed_result')
 error('NMC_fixed_result was not found in:\n%s', cfg.nmc_result_mat);
end

lfp = S_LFP.LFP_fixed_result;
nmc = S_NMC.NMC_fixed_result;

required_lfp_fields = { ...
 's_true', ...
 's_pred_opt', ...
 'Pred_Action_opt', ...
 'J_action', ...
 'opt_T1', ...
 'opt_T2'};

required_nmc_fields = { ...
 's_true', ...
 'Pred_Action_opt', ...
 'J', ...
 'opt_T1', ...
 'opt_T2'};

local_require_fields(lfp, required_lfp_fields, 'LFP_fixed_result');
local_require_fields(nmc, required_nmc_fields, 'NMC_fixed_result');

% NMC predicted-SOH field: output uses s_pred.
[s_pred_NMC, nmc_pred_field] = local_get_first_existing_field( ...
 nmc, {'s_pred', 's_pred_opt'}, 'NMC predicted SOH');

s_true_LFP = double(lfp.s_true(:));
s_true_NMC = double(nmc.s_true(:));

s_pred_LFP = double(lfp.s_pred_opt(:));
s_pred_NMC = double(s_pred_NMC(:));

action_LFP = double(lfp.Pred_Action_opt(:));
action_NMC = double(nmc.Pred_Action_opt(:));

J_LFP = local_orient_sample_action_matrix( ...
 double(lfp.J_action), numel(s_pred_LFP), 'LFP J_action');

J_NMC = local_orient_sample_action_matrix( ...
 double(nmc.J), numel(s_pred_NMC), 'NMC J');

T1_LFP = double(lfp.opt_T1);
T2_LFP = double(lfp.opt_T2);
T1_NMC = double(nmc.opt_T1);
T2_NMC = double(nmc.opt_T2);

% Validate lengths.
n_LFP = numel(s_true_LFP);
n_NMC = numel(s_true_NMC);

if numel(s_pred_LFP) ~= n_LFP || ...
 numel(action_LFP) ~= n_LFP || ...
 size(J_LFP, 1) ~= n_LFP

 error('LFP sample-level arrays do not have consistent lengths.');
end

if numel(s_pred_NMC) ~= n_NMC || ...
 numel(action_NMC) ~= n_NMC || ...
 size(J_NMC, 1) ~= n_NMC

 error('NMC sample-level arrays do not have consistent lengths.');
end

if any(~ismember(action_LFP, [1, 2, 3])) || ...
 any(~ismember(action_NMC, [1, 2, 3]))

 error('Predicted action arrays must contain only action codes 1, 2 and 3.');
end

fprintf('>>> Loaded LFP samples: %d\n', n_LFP);
fprintf('>>> Loaded NMC samples: %d\n', n_NMC);
fprintf('>>> NMC predicted-SOH field used: %s\n', nmc_pred_field);
fprintf('>>> LFP thresholds: T1 = %.4f, T2 = %.4f\n', T1_LFP, T2_LFP);
fprintf('>>> NMC thresholds: T1 = %.4f, T2 = %.4f\n', T1_NMC, T2_NMC);

%% 3) Verify the common simulated true-SOH population
% The alluvial transition matrix is meaningful only when the two chemistry
% models are evaluated on the same ordered population.

if n_LFP ~= n_NMC

 error(['LFP and NMC contain different numbers of simulated packs ' ...
 '(%d versus %d). Rerun Exp01 and Exp02 using the same ' ...
 'N_sim and random seed.'], n_LFP, n_NMC);
end

valid_population = isfinite(s_true_LFP) & isfinite(s_true_NMC);

if ~all(valid_population)
 error('The simulated true-SOH arrays contain non-finite values.');
end

population_soh_abs_difference = abs(s_true_LFP - s_true_NMC);
max_population_soh_difference = max(population_soh_abs_difference);

if max_population_soh_difference > cfg.population_soh_tolerance

 error(['LFP and NMC were not evaluated on the same ordered true-SOH ' ...
 'population.\nMaximum SOH difference = %.6g.\nRerun Exp01 ' ...
 'and Exp02 with the same N_sim and random seed before constructing ' ...
 'an action-transition matrix.'], ...
 max_population_soh_difference);
end

s_true_common = s_true_LFP;
N_common = n_LFP;

fprintf('>>> Common-population check passed.\n');
fprintf('>>> Maximum LFP-NMC true-SOH difference: %.3e\n', ...
 max_population_soh_difference);

%% 4) Re-estimate chemistry-specific expected net-value curves consistently
% Both chemistries are smoothed from sample-level values using the same:
% - predicted-SOH grid;
% - Gaussian kernel;
% - bandwidth;
% - minimum effective-sample-size rule.

s_common = ( ...
 cfg.soh_grid_min : ...
 cfg.soh_grid_step : ...
 cfg.soh_grid_max).';

[V_LFP, Effective_N_LFP] = local_kernel_conditional_mean( ...
 s_pred_LFP, ...
 J_LFP, ...
 s_common, ...
 cfg.kernel_bandwidth, ...
 cfg.minimum_effective_sample_size);

[V_NMC, Effective_N_NMC] = local_kernel_conditional_mean( ...
 s_pred_NMC, ...
 J_NMC, ...
 s_common, ...
 cfg.kernel_bandwidth, ...
 cfg.minimum_effective_sample_size);

% Adjacent-pathway expected net-value gaps.
D32_LFP = V_LFP(:, 3) - V_LFP(:, 2); % A3 - A2
D32_NMC = V_NMC(:, 3) - V_NMC(:, 2);

D12_LFP = V_LFP(:, 1) - V_LFP(:, 2); % A1 - A2
D12_NMC = V_NMC(:, 1) - V_NMC(:, 2);

Shift_D32 = D32_NMC - D32_LFP;
Shift_D12 = D12_NMC - D12_LFP;

CommonGridCurveTable = table( ...
 s_common, ...
 V_LFP(:, 1), ...
 V_LFP(:, 2), ...
 V_LFP(:, 3), ...
 V_NMC(:, 1), ...
 V_NMC(:, 2), ...
 V_NMC(:, 3), ...
 D32_LFP, ...
 D32_NMC, ...
 Shift_D32, ...
 D12_LFP, ...
 D12_NMC, ...
 Shift_D12, ...
 Effective_N_LFP, ...
 Effective_N_NMC, ...
 'VariableNames', { ...
 'Predicted_SOH', ...
 'LFP_A1_expected_net_value', ...
 'LFP_A2_expected_net_value', ...
 'LFP_A3_expected_net_value', ...
 'NMC_A1_expected_net_value', ...
 'NMC_A2_expected_net_value', ...
 'NMC_A3_expected_net_value', ...
 'LFP_A3_minus_A2', ...
 'NMC_A3_minus_A2', ...
 'NMC_minus_LFP_A3_minus_A2', ...
 'LFP_A1_minus_A2', ...
 'NMC_A1_minus_A2', ...
 'NMC_minus_LFP_A1_minus_A2', ...
 'LFP_effective_sample_size', ...
 'NMC_effective_sample_size'});

writetable(CommonGridCurveTable, cfg.curve_csv);

fprintf('>>> Common-grid chemistry curves saved: %s\n', cfg.curve_csv);

%% 5) Locate and export adjacent-pathway zero crossings
curve_names = [ ...
 "LFP A3-A2"; ...
 "NMC A3-A2"; ...
 "LFP A1-A2"; ...
 "NMC A1-A2"];

curve_data = { ...
 D32_LFP; ...
 D32_NMC; ...
 D12_LFP; ...
 D12_NMC};

reference_thresholds = [ ...
 T1_LFP; ...
 T1_NMC; ...
 T2_LFP; ...
 T2_NMC];

All_zero_crossings_SOH = strings(numel(curve_data), 1);
Selected_crossing_SOH = nan(numel(curve_data), 1);
Crossing_minus_optimized_threshold = nan(numel(curve_data), 1);
Number_of_crossings = zeros(numel(curve_data), 1);

fprintf('\n========== Adjacent-pathway expected net-value crossings ==========\n');

for ic = 1:numel(curve_data)

 crossings_now = local_find_zero_crossings( ...
 s_common, curve_data{ic});

 Number_of_crossings(ic) = numel(crossings_now);

 if isempty(crossings_now)

 All_zero_crossings_SOH(ic) = "No zero crossing";

 else

 All_zero_crossings_SOH(ic) = strjoin( ...
 compose('%.4f', crossings_now), ', ');

 [~, selected_idx] = min( ...
 abs(crossings_now - reference_thresholds(ic)));

 Selected_crossing_SOH(ic) = crossings_now(selected_idx);

 Crossing_minus_optimized_threshold(ic) = ...
 Selected_crossing_SOH(ic) - reference_thresholds(ic);
 end

 fprintf('%s: all = %s | selected = %.4f | optimized threshold = %.4f\n', ...
 curve_names(ic), ...
 All_zero_crossings_SOH(ic), ...
 Selected_crossing_SOH(ic), ...
 reference_thresholds(ic));
end

BoundaryGapCrossingTable = table( ...
 curve_names, ...
 Number_of_crossings, ...
 All_zero_crossings_SOH, ...
 Selected_crossing_SOH, ...
 reference_thresholds, ...
 Crossing_minus_optimized_threshold, ...
 'VariableNames', { ...
 'Curve', ...
 'Number_of_crossings', ...
 'All_zero_crossings_SOH', ...
 'Selected_crossing_nearest_optimized_threshold_SOH', ...
 'Optimized_predicted_SOH_threshold', ...
 'Crossing_minus_optimized_threshold_SOH'});

writetable(BoundaryGapCrossingTable, cfg.boundary_gap_csv);

% Selected A3-A2 crossings for Fig. 3d.
s_cross_D32_LFP = Selected_crossing_SOH(1);
s_cross_D32_NMC = Selected_crossing_SOH(2);
delta_cross_D32 = s_cross_D32_NMC - s_cross_D32_LFP;

%% 6) Export value gaps at representative predicted-SOH points
soh_rep = (0.4:0.1:1.0).';

D32_LFP_rep = interp1( ...
 s_common, D32_LFP, soh_rep, 'linear', nan);

D32_NMC_rep = interp1( ...
 s_common, D32_NMC, soh_rep, 'linear', nan);

Shift_D32_rep = interp1( ...
 s_common, Shift_D32, soh_rep, 'linear', nan);

D12_LFP_rep = interp1( ...
 s_common, D12_LFP, soh_rep, 'linear', nan);

D12_NMC_rep = interp1( ...
 s_common, D12_NMC, soh_rep, 'linear', nan);

Shift_D12_rep = interp1( ...
 s_common, Shift_D12, soh_rep, 'linear', nan);

RepresentativeSOHTable = table( ...
 soh_rep, ...
 D32_LFP_rep, ...
 D32_NMC_rep, ...
 Shift_D32_rep, ...
 D12_LFP_rep, ...
 D12_NMC_rep, ...
 Shift_D12_rep, ...
 'VariableNames', { ...
 'Predicted_SOH', ...
 'LFP_A3_minus_A2_USD_per_pack', ...
 'NMC_A3_minus_A2_USD_per_pack', ...
 'NMC_minus_LFP_A3_minus_A2_USD_per_pack', ...
 'LFP_A1_minus_A2_USD_per_pack', ...
 'NMC_A1_minus_A2_USD_per_pack', ...
 'NMC_minus_LFP_A1_minus_A2_USD_per_pack'});

writetable(RepresentativeSOHTable, cfg.representative_soh_csv);

disp(' ');
disp('========== Expected net-value gaps at representative predicted SOH ==========');
disp(RepresentativeSOHTable);

%% 7) Main Fig. 3f: chemistry-dependent A3-A2 expected net-value gap
color_LFP = [0.000, 0.447, 0.741];
color_NMC = [0.850, 0.325, 0.098];
color_fill = [0.82, 0.82, 0.82];

valid_gap = isfinite(D32_LFP) & isfinite(D32_NMC);

if sum(valid_gap) < 2
 error('Not enough valid A3-A2 gap values were available for plotting.');
end

fig_D32 = figure( ...
 'Color', 'w', ...
 'Name', 'MainFig03f_refurbishment_recycling_value_gap', ...
 'Position', [100, 80, 920, 620]);

hold on;
box on;
grid on;

x_valid = s_common(valid_gap);
lfp_valid = D32_LFP(valid_gap);
nmc_valid = D32_NMC(valid_gap);

h_fill = fill( ...
 [x_valid; flipud(x_valid)], ...
 [nmc_valid; flipud(lfp_valid)], ...
 color_fill, ...
 'EdgeColor', 'none', ...
 'FaceAlpha', 0.35, ...
 'DisplayName', 'NMC-LFP difference');

h_lfp = plot( ...
 s_common, ...
 D32_LFP, ...
 'LineWidth', 2.8, ...
 'Color', color_LFP, ...
 'DisplayName', 'LFP');

h_nmc = plot( ...
 s_common, ...
 D32_NMC, ...
 'LineWidth', 2.8, ...
 'Color', color_NMC, ...
 'DisplayName', 'NMC');

yline(0, ...
 'k-', ...
 'LineWidth', 1.2, ...
 'HandleVisibility', 'off');

% Dynamic vertical range.
all_gap_values = [lfp_valid; nmc_valid];
gap_y_min = min(all_gap_values);
gap_y_max = max(all_gap_values);
gap_y_span = gap_y_max - gap_y_min;

if ~isfinite(gap_y_span) || gap_y_span <= 0
 gap_y_span = max(abs(all_gap_values));
end

if ~isfinite(gap_y_span) || gap_y_span <= 0
 gap_y_span = 1;
end

plot_y_limits = [ ...
 gap_y_min - 0.10 * gap_y_span, ...
 gap_y_max + 0.22 * gap_y_span];

% Mark the selected predicted-SOH crossings.
if isfinite(s_cross_D32_LFP)

 xline( ...
 s_cross_D32_LFP, ...
 '--', ...
 'Color', color_LFP, ...
 'LineWidth', 1.5, ...
 'HandleVisibility', 'off');

 scatter( ...
 s_cross_D32_LFP, ...
 0, ...
 70, ...
 'MarkerFaceColor', color_LFP, ...
 'MarkerEdgeColor', 'w', ...
 'LineWidth', 1.0, ...
 'HandleVisibility', 'off');

 text( ...
 s_cross_D32_LFP, ...
 plot_y_limits(2) - 0.08 * diff(plot_y_limits), ...
 sprintf('LFP crossing = %.3f', s_cross_D32_LFP), ...
 'HorizontalAlignment', 'center', ...
 'VerticalAlignment', 'top', ...
 'Color', color_LFP, ...
 'FontName', 'Arial', ...
 'FontSize', 10.5, ...
 'FontWeight', 'bold');
end

if isfinite(s_cross_D32_NMC)

 xline( ...
 s_cross_D32_NMC, ...
 '--', ...
 'Color', color_NMC, ...
 'LineWidth', 1.5, ...
 'HandleVisibility', 'off');

 scatter( ...
 s_cross_D32_NMC, ...
 0, ...
 70, ...
 'MarkerFaceColor', color_NMC, ...
 'MarkerEdgeColor', 'w', ...
 'LineWidth', 1.0, ...
 'HandleVisibility', 'off');

 text( ...
 s_cross_D32_NMC, ...
 plot_y_limits(2) - 0.18 * diff(plot_y_limits), ...
 sprintf('NMC crossing = %.3f', s_cross_D32_NMC), ...
 'HorizontalAlignment', 'center', ...
 'VerticalAlignment', 'top', ...
 'Color', color_NMC, ...
 'FontName', 'Arial', ...
 'FontSize', 10.5, ...
 'FontWeight', 'bold');
end

if isfinite(delta_cross_D32)

 text( ...
 mean([s_cross_D32_LFP, s_cross_D32_NMC]), ...
 plot_y_limits(2) - 0.30 * diff(plot_y_limits), ...
 sprintf('\\Delta crossing = %.3f SOH', delta_cross_D32), ...
 'HorizontalAlignment', 'center', ...
 'VerticalAlignment', 'top', ...
 'Color', [0.15, 0.15, 0.15], ...
 'FontName', 'Arial', ...
 'FontSize', 10.5, ...
 'FontWeight', 'bold');
end

xlabel( ...
 'Predicted SOH', ...
 'FontName', 'Arial', ...
 'FontSize', 13);

ylabel( ...
 'Expected net-value difference, A_3 - A_2 (USD per pack)', ...
 'FontName', 'Arial', ...
 'FontSize', 13);

title( ...
 'Chemistry-dependent refurbishment-recycling value gap', ...
 'FontName', 'Arial', ...
 'FontSize', 14, ...
 'FontWeight', 'normal');

legend( ...
 [h_lfp, h_nmc, h_fill], ...
 {'LFP', 'NMC', 'NMC-LFP difference'}, ...
 'Location', 'northeast', ...
 'Box', 'off');

xlim([cfg.soh_grid_min, cfg.soh_grid_max]);
ylim(plot_y_limits);

set(gca, ...
 'FontName', 'Arial', ...
 'FontSize', 12, ...
 'LineWidth', 1.1, ...
 'TickDir', 'out', ...
 'GridLineStyle', ':', ...
 'GridAlpha', 0.25);

local_save_figure( ...
 fig_D32, ...
 cfg.fig_value_gap, ...
 cfg.png_value_gap, ...
 cfg.png_resolution);

fprintf('>>> Saved Main Fig. 3f: %s\n', cfg.png_value_gap);

%% 8) Construct chemistry-specific action transitions on the common population
% Unlike the original implementation, this section does not apply both
% chemistry thresholds to the LFP predicted-SOH vector. It uses the actual
% optimized action arrays produced independently by Exp01 and Exp02.

disp_order = [3, 2, 1];
disp_names = {'A3: Recycle', 'A2: Refurbish', 'A1: Direct reuse'};
disp_short = {'A3', 'A2', 'A1'};

M_count = zeros(3, 3);

for ii = 1:3
 for jj = 1:3

 M_count(ii, jj) = sum( ...
 action_LFP == disp_order(ii) & ...
 action_NMC == disp_order(jj));
 end
end

M_share = 100 .* M_count ./ N_common;

left_share = sum(M_share, 2);
right_share = sum(M_share, 1).';

% Complete 3-by-3 transition table, including zero-flow cells.
Action_under_LFP = strings(9, 1);
Action_under_NMC = strings(9, 1);
Count = zeros(9, 1);
Share_percent = zeros(9, 1);

row_counter = 0;

for ii = 1:3
 for jj = 1:3

 row_counter = row_counter + 1;

 Action_under_LFP(row_counter) = disp_short{ii};
 Action_under_NMC(row_counter) = disp_short{jj};
 Count(row_counter) = M_count(ii, jj);
 Share_percent(row_counter) = M_share(ii, jj);
 end
end

ActionTransitionTable = table( ...
 Action_under_LFP, ...
 Action_under_NMC, ...
 Count, ...
 Share_percent);

writetable(ActionTransitionTable, cfg.transition_csv);

Action = string(disp_short(:));

ActionShareTable = table( ...
 Action, ...
 left_share, ...
 right_share, ...
 right_share - left_share, ...
 'VariableNames', { ...
 'Action', ...
 'LFP_share_percent', ...
 'NMC_share_percent', ...
 'NMC_minus_LFP_share_percentage_points'});

writetable(ActionShareTable, cfg.action_share_csv);

disp(' ');
disp('========== LFP-to-NMC optimized action transition matrix ==========');
disp(ActionTransitionTable);

disp(' ');
disp('========== Chemistry-specific optimized action shares ==========');
disp(ActionShareTable);

%% 9) Main Fig. 3e: alluvial-style chemistry reallocation plot
color_A3 = [0.850, 0.325, 0.098];
color_A2 = [0.466, 0.674, 0.188];
color_A1 = [0.000, 0.447, 0.741];

color_map = [ ...
 color_A3; ...
 color_A2; ...
 color_A1];

fig_alluvial = figure( ...
 'Color', 'w', ...
 'Name', 'MainFig03e_LFP_NMC_action_share_reallocation', ...
 'Position', [120, 80, 980, 620]);

ax = axes('Parent', fig_alluvial);

hold(ax, 'on');
axis(ax, [0, 1, 0, 1]);
axis(ax, 'off');

xL0 = 0.16;
xL1 = 0.22;
xR0 = 0.78;
xR1 = 0.84;

H_total = 0.72;
top_y = 0.86;
gap_y = 0.03;
scale_y = H_total / 100;

left_y0 = zeros(3, 1);
left_y1 = zeros(3, 1);
right_y0 = zeros(3, 1);
right_y1 = zeros(3, 1);

% Left-side blocks.
y_cursor = top_y;

for ii = 1:3

