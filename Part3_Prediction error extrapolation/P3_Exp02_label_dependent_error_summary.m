%% Local helper functions
function A = local_orient_numeric_by_k(A, n_sizes, variable_name)
% Ensure that a numeric k-dependent array has k along its first dimension.

    if size(A, 1) == n_sizes
        return;
    end

    if size(A, 2) == n_sizes
        A = A.';
        return;
    end

    error(['Variable "%s" has size %s, which is incompatible with ' ...
        '%d k values.'], variable_name, mat2str(size(A)), n_sizes);
end

function C = local_orient_cell_by_k(C, n_sizes, variable_name)
% Ensure that a cell array has k along its first dimension.

    if ~iscell(C)
        error('Variable "%s" must be a cell array.', variable_name);
    end

    if size(C, 1) == n_sizes
        return;
    end

    if size(C, 2) == n_sizes
        C = C.';
        return;
    end

    error(['Variable "%s" has size %s, which is incompatible with ' ...
        '%d k values.'], variable_name, mat2str(size(C)), n_sizes);
end

function C = local_orient_cell_vector_by_k(C, n_sizes, variable_name)
% Convert a k-dependent cell vector to an n_sizes-by-1 column vector.

    if ~iscell(C)
        error('Variable "%s" must be a cell array.', variable_name);
    end

    if numel(C) ~= n_sizes
        error(['Variable "%s" contains %d cells, but %d k values ' ...
            'were expected.'], variable_name, numel(C), n_sizes);
    end

    C = C(:);
end

function completed_repeats = local_completed_outer_repeats(res_RMSE_outer, D)
% Determine how many outer repetitions contain completed results.

    n_available = size(res_RMSE_outer, 2);

    if isfield(D, 'b') && isscalar(D.b) && isfinite(D.b)
        completed_repeats = min(double(D.b), n_available);
    else
        valid_cols = any(isfinite(res_RMSE_outer) & ...
            res_RMSE_outer > 0, 1);

        if any(valid_cols)
            completed_repeats = find(valid_cols, 1, 'last');
        else
            error('No completed outer repetitions were detected.');
        end
    end

    completed_repeats = max(1, completed_repeats);
end

function pooled_err = local_pool_all_inner_residuals(err_cells)
% Pool all finite residual realizations from one k across outer and inner runs.

    pooled_err = zeros(0, 1);

    for j = 1:numel(err_cells)
        curr_err = err_cells{j};

        if isempty(curr_err)
            continue;
        end

        curr_err = double(curr_err(:));
        curr_err = curr_err(isfinite(curr_err));
        pooled_err = [pooled_err; curr_err]; %#ok<AGROW>
    end
end

function local_save_figure(fig_handle, fig_file, png_file, resolution)
% Save a figure as both FIG and PNG files.

    out_dir = fileparts(fig_file);
    if ~exist(out_dir, 'dir')
        [status, message] = mkdir(out_dir);
        if ~status
            error('Unable to create figure directory:\n%s\nReason: %s', ...
                out_dir, message);
        end
    end

    savefig(fig_handle, fig_file);
    exportgraphics(fig_handle, png_file, 'Resolution', resolution);
    fprintf('[Figure] Saved: %s\n', png_file);
end
