%% P3_Exp04_ResidualRegionBoundaryRobustness.m
% Purpose:
% Evaluate whether the empirical residual-feature trends used in Part 3
% remain stable when the fixed near-zero and positive right-tail region
% boundaries are narrowed or widened by 20%.
%
% Design principles:
%   1) all-inner residuals are the only empirical source;
%   2) raw empirical features and three-point-smoothed fitting inputs are
%      stored separately;
%   3) the same functional forms and fitting constraints as Exp03 are used;
%   4) this experiment is a feature-definition robustness check only.
%      It does not rebuild the full residual generator or propagate each
%      boundary scenario into Part 4 or Part 5;
%   5) distribution illustrations use empirical residuals only, so changing
%      a diagnostic boundary does not also change the underlying residual
%      distribution being displayed.

clc;
clear;
close all;

%% 1) Configuration
cfg = struct();

script_dir = fileparts(mfilename('fullpath'));
if isempty(script_dir)
    script_dir = pwd;
end

cfg.output_dir  = fullfile(script_dir, 'Output');
cfg.results_dir = fullfile(cfg.output_dir, 'Results');

cfg.figure_root = fullfile(script_dir, 'Figures');
cfg.supp_fig_dir = fullfile(cfg.figure_root, 'Supplementary');

cfg.input_mat = fullfile(cfg.results_dir, ...
    'P3_Exp01_NestedResampling_Final.mat');

cfg.fit_summary_csv = fullfile(cfg.results_dir, ...
    'P3_Exp04_ResidualRegionBoundaryRobustness_FitSummary.csv');
cfg.feature_curves_csv = fullfile(cfg.results_dir, ...
    'P3_Exp04_ResidualRegionBoundaryRobustness_FeatureCurves.csv');
cfg.fit_curves_csv = fullfile(cfg.results_dir, ...
    'P3_Exp04_ResidualRegionBoundaryRobustness_FitCurves.csv');
cfg.trend_check_csv = fullfile(cfg.results_dir, ...
    'P3_Exp04_ResidualRegionBoundaryRobustness_TrendCheck.csv');
cfg.all_inner_counts_csv = fullfile(cfg.results_dir, ...
    'P3_Exp04_ResidualRegionBoundaryRobustness_AllInnerCounts.csv');
cfg.model_mat = fullfile(cfg.results_dir, ...
    'P3_Exp04_ResidualRegionBoundaryRobustness_Model.mat');

cfg.fig_r2_summary = fullfile(cfg.supp_fig_dir, ...
    'SupFig12a_ResidualRegionBoundary_R2_summary.fig');
cfg.png_r2_summary = fullfile(cfg.supp_fig_dir, ...
    'SupFig12a_ResidualRegionBoundary_R2_summary.png');

cfg.fig_PM = fullfile(cfg.supp_fig_dir, ...
    'SupFig12b_ResidualRegionBoundary_PM_sensitivity.fig');
cfg.png_PM = fullfile(cfg.supp_fig_dir, ...
    'SupFig12b_ResidualRegionBoundary_PM_sensitivity.png');

cfg.fig_PR = fullfile(cfg.supp_fig_dir, ...
    'SupFig12c_ResidualRegionBoundary_PR_sensitivity.fig');
cfg.png_PR = fullfile(cfg.supp_fig_dir, ...
    'SupFig12c_ResidualRegionBoundary_PR_sensitivity.png');

cfg.fig_sigR = fullfile(cfg.supp_fig_dir, ...
    'SupFig12d_ResidualRegionBoundary_SigmaR_sensitivity.fig');
cfg.png_sigR = fullfile(cfg.supp_fig_dir, ...
    'SupFig12d_ResidualRegionBoundary_SigmaR_sensitivity.png');

cfg.dist_dir = fullfile(cfg.supp_fig_dir, ...
    'SupFig12_residual_region_distributions_empirical_k05_to_k20');

cfg.png_resolution = 600;
cfg.do_plot_distribution = true;

folders = {cfg.output_dir, cfg.results_dir, cfg.figure_root, ...
    cfg.supp_fig_dir, cfg.dist_dir};
for i = 1:numel(folders)
    if ~exist(folders{i}, 'dir')
        mkdir(folders{i});
    end
end

fprintf('================ P3 Exp04: residual-region boundary robustness ================\n');
fprintf('Input result file: %s\n', cfg.input_mat);
fprintf('Output folder:     %s\n', cfg.results_dir);
fprintf('Figure folder:     %s\n', cfg.supp_fig_dir);

%% 2) Analysis settings
cfg.k_emp_min = 5;
cfg.k_emp_max = 21;
cfg.k_extrap_max = 50;

k_emp = (cfg.k_emp_min:cfg.k_emp_max)';
k_vals = (cfg.k_emp_min:cfg.k_extrap_max)';

% Only empirical distributions are illustrated in this robustness check.
cfg.plot_k_list = [5, 10, 15, 20];

% Baseline fixed diagnostic regions from Exp03.
base_main  = [-0.012,  0.015];
base_right = [ 0.028,  0.052];

% Region-width perturbation scenarios.
ScenarioName = [
    "Baseline"
    "Narrow_all_20"
    "Wide_all_20"
    "Narrow_main_20"
    "Wide_main_20"
    "Narrow_right_20"
    "Wide_right_20"
    ];

main_mult = [1.0; 0.8; 1.2; 0.8; 1.2; 1.0; 1.0];
right_mult = [1.0; 0.8; 1.2; 1.0; 1.0; 0.8; 1.2];

nScenario = numel(ScenarioName);

% Density-plot settings.
cfg.kde_bw = 0.0055;
cfg.hist_edges = linspace(-0.06, 0.06, 70);

%% 3) Load and pool all-inner residuals
if ~exist(cfg.input_mat, 'file')
    error(['Input data file not found:\n%s\n' ...
        'Run P3_Exp01_NestedResampling_ErrorEvaluation.m first.'], ...
        cfg.input_mat);
end

S = load(cfg.input_mat);

required_vars = {'train_size_list', 'all_inner_errs'};
for i = 1:numel(required_vars)
    if ~isfield(S, required_vars{i})
        error('Required variable "%s" is missing from %s.', ...
            required_vars{i}, cfg.input_mat);
    end
end

train_size_list = double(S.train_size_list(:));
all_inner_errs = S.all_inner_errs;

num_k_emp = numel(k_emp);
err_cell = cell(num_k_emp, 1);

num_outer_used_list = zeros(num_k_emp, 1);
num_inner_used_list = zeros(num_k_emp, 1);
num_residual_used_list = zeros(num_k_emp, 1);

for i = 1:num_k_emp
    k_now = k_emp(i);
    idx_k = find(train_size_list == k_now, 1);

    if isempty(idx_k)
        error('k = %d was not found in train_size_list.', k_now);
    end

    err_all = [];
    num_outer_used = 0;
    num_inner_used = 0;

    for outer_id = 1:size(all_inner_errs, 2)
        E = all_inner_errs{idx_k, outer_id};

        if isempty(E)
            continue;
        end

        E = double(E);
        if all(~isfinite(E(:)))
            continue;
        end

        num_outer_used = num_outer_used + 1;
        num_inner_used = num_inner_used + size(E, 2);

        E = E(isfinite(E));
        err_all = [err_all; E(:)]; %#ok<AGROW>
    end

    if isempty(err_all)
        error('No valid all-inner residuals were found for k = %d.', k_now);
    end

    err_cell{i} = err_all(:);
    num_outer_used_list(i) = num_outer_used;
    num_inner_used_list(i) = num_inner_used;
    num_residual_used_list(i) = numel(err_all);

    fprintf(['k = %d | outer runs = %d | inner runs = %d | ' ...
        'all-inner residual samples = %d\n'], ...
        k_now, num_outer_used, num_inner_used, numel(err_all));
end

AllInnerCountTable = table( ...
    k_emp, ...
    num_outer_used_list, ...
    num_inner_used_list, ...
    num_residual_used_list, ...
    'VariableNames', { ...
    'k', ...
    'N_outer_runs', ...
    'N_inner_runs_all_inner', ...
    'N_residuals_all_inner'});

writetable(AllInnerCountTable, cfg.all_inner_counts_csv);

%% 4) Scenario-wise feature extraction and fitting
FitSummary_All = table();
FeatureCurve_All = table();
FitCurve_All = table();
Store = struct();

for ss = 1:nScenario
    curr_name = ScenarioName(ss);

    region_main = local_scaled_region(base_main, main_mult(ss));
    region_right = local_scaled_region(base_right, right_mult(ss));

    fprintf('\n======================================================\n');
    fprintf('Current scenario: %s\n', curr_name);
    fprintf('Near-zero region = [%.5f, %.5f]\n', ...
        region_main(1), region_main(2));
    fprintf('Right-tail region = [%.5f, %.5f]\n', ...
        region_right(1), region_right(2));
    fprintf('======================================================\n');

    % Raw empirical features.
    P_M_raw = nan(num_k_emp, 1);
    P_R_raw = nan(num_k_emp, 1);
    mu_M_raw = nan(num_k_emp, 1);
    mu_R_raw = nan(num_k_emp, 1);
    sig_M_raw = nan(num_k_emp, 1);
    sig_R_raw = nan(num_k_emp, 1);

    for ii = 1:num_k_emp
        e = err_cell{ii};

        idx_main = (e >= region_main(1)) & (e < region_main(2));
        idx_right = (e >= region_right(1)) & (e < region_right(2));

        P_M_raw(ii) = mean(idx_main);
        P_R_raw(ii) = mean(idx_right);

        eM = e(idx_main);
        if isempty(eM)
            eM = e;
        end

        mu_M_raw(ii) = median(eM);
        sig_M_raw(ii) = local_robust_sigma(eM, 1e-4);

        eR = e(idx_right);
        if isempty(eR)
            mu_R_raw(ii) = mean(region_right);
            sig_R_raw(ii) = 0.005;
        else
            mu_R_raw(ii) = mean(eR);
            sig_R_raw(ii) = local_robust_sigma(eR, 1e-4);
        end
    end

    % Three-point moving averages are fitting inputs only.
    P_M_fit_input = min(max( ...
        smoothdata(P_M_raw, 'movmean', 3), 0), 1);
    P_R_fit_input = min(max( ...
        smoothdata(P_R_raw, 'movmean', 3), 0), 0.40);
    mu_M_fit_input = smoothdata(mu_M_raw, 'movmean', 3);
    mu_R_fit_input = smoothdata(mu_R_raw, 'movmean', 3);
    sig_M_fit_input = max( ...
        smoothdata(sig_M_raw, 'movmean', 3), 1e-4);
    sig_R_fit_input = max( ...
        smoothdata(sig_R_raw, 'movmean', 3), 1e-4);

    % Apply the same curve structures and constraints as Exp03.
    fit_PR = local_fit_PR(k_emp, P_R_fit_input);
    fit_PM = local_fit_PM(k_emp, P_M_fit_input);
    fit_muM = local_fit_muM(k_emp, mu_M_fit_input, cfg.k_emp_max);
    fit_sigR = local_fit_sigR(k_emp, sig_R_fit_input);

    PR_fit_emp = fit_PR.predict(k_emp);
    PR_fit_full = fit_PR.predict(k_vals);

    PM_fit_emp = fit_PM.predict(k_emp);
    PM_fit_full = fit_PM.predict(k_vals);

    muM_fit_full = fit_muM.predict(k_vals);

    sigR_fit_emp = fit_sigR.predict(k_emp);
    sigR_fit_full = fit_sigR.predict(k_vals);

    % R-squared against both raw observations and smoothed fitting inputs.
    R2_PR_raw = local_r2(P_R_raw, PR_fit_emp);
    R2_PR_fit_input = local_r2(P_R_fit_input, PR_fit_emp);

    R2_PM_raw = local_r2(P_M_raw, PM_fit_emp);
    R2_PM_fit_input = local_r2(P_M_fit_input, PM_fit_emp);

    muM_fit_emp = fit_muM.predict(k_emp);
    R2_muM_raw = local_r2(mu_M_raw, muM_fit_emp);
    R2_muM_fit_input = local_r2(mu_M_fit_input, muM_fit_emp);

    R2_sigR_raw = local_r2(sig_R_raw, sigR_fit_emp);
    R2_sigR_fit_input = local_r2(sig_R_fit_input, sigR_fit_emp);

    % Trend checks use raw empirical endpoints and fitted endpoints.
    Delta_PM_raw = P_M_raw(end) - P_M_raw(1);
    Delta_PR_raw = P_R_raw(end) - P_R_raw(1);
    Delta_sigR_raw = sig_R_raw(end) - sig_R_raw(1);

    Delta_PM_fit = PM_fit_emp(end) - PM_fit_emp(1);
    Delta_PR_fit = PR_fit_emp(end) - PR_fit_emp(1);
    Delta_sigR_fit = sigR_fit_emp(end) - sigR_fit_emp(1);

    FitSummary = table( ...
        curr_name, ...
        main_mult(ss), ...
        right_mult(ss), ...
        region_main(1), ...
        region_main(2), ...
        region_right(1), ...
        region_right(2), ...
        R2_PM_raw, ...
        R2_PM_fit_input, ...
        R2_PR_raw, ...
        R2_PR_fit_input, ...
        R2_muM_raw, ...
        R2_muM_fit_input, ...
        R2_sigR_raw, ...
        R2_sigR_fit_input, ...
        Delta_PM_raw, ...
        Delta_PR_raw, ...
        Delta_sigR_raw, ...
        Delta_PM_fit, ...
        Delta_PR_fit, ...
        Delta_sigR_fit, ...
        fit_PM.rate, ...
        fit_PM.plateau, ...
        fit_PR.rate, ...
        fit_PR.plateau, ...
        fit_sigR.amplitude, ...
        fit_sigR.rate, ...
        fit_sigR.plateau, ...
        'VariableNames', { ...
        'Scenario', ...
        'MainWidthMultiplier', ...
        'RightWidthMultiplier', ...
        'MainLower', ...
        'MainUpper', ...
        'RightLower', ...
        'RightUpper', ...
        'R2_PM_raw', ...
        'R2_PM_fit_input', ...
        'R2_PR_raw', ...
        'R2_PR_fit_input', ...
        'R2_muM_raw', ...
        'R2_muM_fit_input', ...
        'R2_sigR_raw', ...
        'R2_sigR_fit_input', ...
        'Delta_PM_raw_k21_minus_k5', ...
        'Delta_PR_raw_k21_minus_k5', ...
        'Delta_sigR_raw_k21_minus_k5', ...
        'Delta_PM_fit_k21_minus_k5', ...
        'Delta_PR_fit_k21_minus_k5', ...
        'Delta_sigR_fit_k21_minus_k5', ...
        'PM_growth_rate', ...
        'PM_plateau', ...
        'PR_decay_rate', ...
        'PR_plateau', ...
        'sigR_amplitude', ...
        'sigR_decay_rate', ...
        'sigR_plateau'});

    FitSummary_All = [FitSummary_All; FitSummary]; %#ok<AGROW>

    FeatureTable = table( ...
        repmat(curr_name, num_k_emp, 1), ...
        k_emp, ...
        num_outer_used_list, ...
        num_inner_used_list, ...
        num_residual_used_list, ...
        repmat(region_main(1), num_k_emp, 1), ...
        repmat(region_main(2), num_k_emp, 1), ...
        repmat(region_right(1), num_k_emp, 1), ...
        repmat(region_right(2), num_k_emp, 1), ...
        P_M_raw, ...
        P_M_fit_input, ...
        P_R_raw, ...
        P_R_fit_input, ...
        mu_M_raw, ...
        mu_M_fit_input, ...
        sig_M_raw, ...
        sig_M_fit_input, ...
        mu_R_raw, ...
        mu_R_fit_input, ...
        sig_R_raw, ...
        sig_R_fit_input, ...
        'VariableNames', { ...
        'Scenario', ...
        'k', ...
        'N_outer_runs', ...
        'N_inner_runs_all_inner', ...
        'N_residuals_all_inner', ...
        'MainLower', ...
        'MainUpper', ...
        'RightLower', ...
        'RightUpper', ...
        'P_M_raw', ...
        'P_M_fit_input', ...
        'P_R_raw', ...
        'P_R_fit_input', ...
        'mu_M_raw', ...
        'mu_M_fit_input', ...
        'sig_M_raw', ...
        'sig_M_fit_input', ...
        'mu_R_raw', ...
        'mu_R_fit_input', ...
        'sig_R_raw', ...
        'sig_R_fit_input'});

    FeatureCurve_All = [FeatureCurve_All; FeatureTable]; %#ok<AGROW>

    FitCurveTable = table( ...
        repmat(curr_name, numel(k_vals), 1), ...
        k_vals, ...
        PM_fit_full, ...
        PR_fit_full, ...
        muM_fit_full, ...
        sigR_fit_full, ...
        2 * sigR_fit_full, ...
        'VariableNames', { ...
        'Scenario', ...
        'k', ...
        'P_M_fit', ...
        'P_R_fit', ...
        'mu_M_fit', ...
        'sigma_R_fit', ...
        'RightTailWidth_2sigma_R_fit'});

    FitCurve_All = [FitCurve_All; FitCurveTable]; %#ok<AGROW>

    Store(ss).Scenario = curr_name;
    Store(ss).region_main = region_main;
    Store(ss).region_right = region_right;
    Store(ss).k_emp = k_emp;
    Store(ss).k_vals = k_vals;

    Store(ss).P_M_raw = P_M_raw;
    Store(ss).P_M_fit_input = P_M_fit_input;
    Store(ss).P_R_raw = P_R_raw;
    Store(ss).P_R_fit_input = P_R_fit_input;
    Store(ss).sig_R_raw = sig_R_raw;
    Store(ss).sig_R_fit_input = sig_R_fit_input;

    Store(ss).PM_fit_full = PM_fit_full;
    Store(ss).PR_fit_full = PR_fit_full;
    Store(ss).sigR_fit_full = sigR_fit_full;
end

