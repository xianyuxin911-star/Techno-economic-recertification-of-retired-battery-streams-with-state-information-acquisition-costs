function region_out = local_scaled_region(region_in, width_multiplier)
center = mean(region_in);
half_width = 0.5 * (region_in(2) - region_in(1));
region_out = [ ...
    center - width_multiplier * half_width, ...
    center + width_multiplier * half_width];
end

function sigma_value = local_robust_sigma(x, minimum_sigma)
x = x(isfinite(x));
if isempty(x)
    sigma_value = minimum_sigma;
    return;
end

sigma_value = iqr(x) / 1.349;
if numel(x) < 4 || ~isfinite(sigma_value) || sigma_value <= 0
    sigma_value = std(x);
end

if ~isfinite(sigma_value) || sigma_value <= 0
    sigma_value = minimum_sigma;
end

sigma_value = max(sigma_value, minimum_sigma);
end

function fit_result = local_fit_PR(k, y)
k0 = min(k);
y0 = y(1);

huber_loss = @(obs, pred) local_huber_loss(obs, pred);

c_lb = 0.00;
c_ub = 0.08;

bound_c = @(c) min(max(c, c_lb), c_ub);
penalty_c = @(c) ...
    1e6 * (c < c_lb) .* (c_lb - c).^2 + ...
    1e6 * (c > c_ub) .* (c - c_ub).^2;

predict_from_p = @(p, x) ...
    bound_c(p(2)) + ...
    (y0 - bound_c(p(2))) .* exp(-exp(p(1)) .* (x - k0));

objective = @(p) ...
    huber_loss(y, predict_from_p(p, k)) + penalty_c(p(2));

options = optimset( ...
    'Display', 'off', ...
    'MaxFunEvals', 6000, ...
    'MaxIter', 6000);

p = fminsearch(objective, ...
    [log(0.18); min(y(end), 0.05)], options);

fit_result.rate = exp(p(1));
fit_result.plateau = bound_c(p(2));
fit_result.predict = @(x) ...
    fit_result.plateau + ...
    (y0 - fit_result.plateau) .* ...
    exp(-fit_result.rate .* (x - k0));
end

function fit_result = local_fit_PM(k, y)
k0 = min(k);
y0 = y(1);

c_lb = max(y);
c_ub = max(0.95, c_lb + 1e-4);

bound_c = @(c) min(max(c, c_lb), c_ub);
penalty_c = @(c) ...
    1e6 * (c < c_lb) .* (c_lb - c).^2 + ...
    1e6 * (c > c_ub) .* (c - c_ub).^2;

predict_from_p = @(p, x) ...
    bound_c(p(2)) - ...
    (bound_c(p(2)) - y0) .* exp(-exp(p(1)) .* (x - k0));

objective = @(p) ...
    local_huber_loss(y, predict_from_p(p, k)) + penalty_c(p(2));

options = optimset( ...
    'Display', 'off', ...
    'MaxFunEvals', 6000, ...
    'MaxIter', 6000);

p = fminsearch(objective, ...
    [log(0.12); max(0.8, max(y))], options);

fit_result.rate = exp(p(1));
fit_result.plateau = bound_c(p(2));
fit_result.predict = @(x) ...
    fit_result.plateau - ...
    (fit_result.plateau - y0) .* ...
    exp(-fit_result.rate .* (x - k0));
end

function fit_result = local_fit_muM(k, y, anchor_k)
mask = (k >= 7) & (k <= anchor_k) & isfinite(y);
if sum(mask) < 3
    mask = isfinite(y);
end

idx_anchor = find(k == anchor_k, 1);
anchor_value = y(idx_anchor);

predict_from_p = @(p, x) ...
    anchor_value .* exp(-exp(p(1)) .* (x - anchor_k));

objective = @(p) ...
    local_huber_loss(y(mask), predict_from_p(p, k(mask)));

options = optimset( ...
    'Display', 'off', ...
    'MaxFunEvals', 6000, ...
    'MaxIter', 6000);

p = fminsearch(objective, log(0.08), options);

fit_result.rate = exp(p(1));
fit_result.anchor = anchor_value;
fit_result.predict = @(x) ...
    anchor_value .* exp(-fit_result.rate .* (x - anchor_k));
end

function fit_result = local_fit_sigR(k, y)
mask = isfinite(y) & (y > 0);
x = k(mask);
obs = y(mask);

if numel(x) < 3
    error('Not enough valid sigma_R values for fitting.');
end

k0 = min(x);

c_lb = 0.0003;
c_ub = 0.0012;

bound_c = @(c) min(max(c, c_lb), c_ub);
penalty_c = @(c) ...
    1e6 * (c < c_lb) .* (c_lb - c).^2 + ...
    1e6 * (c > c_ub) .* (c - c_ub).^2;

predict_from_p = @(p, xx) ...
    bound_c(p(3)) + ...
    exp(p(1)) .* exp(-exp(p(2)) .* (xx - k0));

objective = @(p) ...
    local_huber_loss(obs, predict_from_p(p, x)) + penalty_c(p(3));

options = optimset( ...
    'Display', 'off', ...
    'MaxFunEvals', 6000, ...
    'MaxIter', 6000);

initial_amplitude = max(max(obs) - min(obs), 1e-5);
p = fminsearch(objective, ...
    [log(initial_amplitude); log(0.18); min(obs)], options);

fit_result.amplitude = exp(p(1));
fit_result.rate = exp(p(2));
fit_result.plateau = bound_c(p(3));
fit_result.predict = @(xx) ...
    fit_result.plateau + ...
    fit_result.amplitude .* exp(-fit_result.rate .* (xx - k0));
end

function loss_value = local_huber_loss(y, yhat)
residual = y(:) - yhat(:);
delta = 1.5 * max(mad(residual, 1), 1e-6);

abs_residual = abs(residual);
loss_each = zeros(size(residual));

small = abs_residual <= delta;
loss_each(small) = 0.5 * residual(small).^2;
loss_each(~small) = ...
    delta .* (abs_residual(~small) - 0.5 * delta);

loss_value = sum(loss_each, 'omitnan');
end

function R2 = local_r2(y, yhat)
y = y(:);
yhat = yhat(:);

valid = isfinite(y) & isfinite(yhat);
y = y(valid);
yhat = yhat(valid);

if numel(y) < 2
    R2 = NaN;
    return;
end

den = sum((y - mean(y)).^2);
if den <= 0
    R2 = NaN;
else
    R2 = 1 - sum((y - yhat).^2) / den;
end
end

